# =============================================================================
# OPTION 1: SSLO CONFIGURATION DUMP
# =============================================================================

# Option 1: Record an SSLO snapshot with paired dependency manifest
function Invoke-SsloDump {
    Clear-Host
    Write-LogSection "Record SSLO Snapshot"
    Write-Host ""
    
    # -------------------------------------------------------------------------
    # Step 1: Retrieve all iAppsLX blocks
    # -------------------------------------------------------------------------
    Write-LogStep "Recording SSLO configuration..."
    $result = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks"
    
    if (-not $result.Success) {
        Write-LogError "Failed to retrieve iAppsLX blocks. HTTP $($result.StatusCode)"
        return $false
    }
    
    $allBlocks = $result.Response.items
    if (-not $allBlocks -or $allBlocks.Count -eq 0) {
        Write-LogWarn "No iAppsLX blocks found on this device."
        return $false
    }
    
    # -------------------------------------------------------------------------
    # Step 2: Inventory all SSLO-related blocks
    # -------------------------------------------------------------------------
    $ssloInventory = @()
    $frameworkCount = 0
    
    foreach ($block in $allBlocks) {
        if (Test-IsFrameworkBlock -Block $block) {
            $frameworkCount++
            continue
        }
        if (-not (Test-IsSsloBlock -Block $block)) { continue }
        
        $depInfo = Get-DeploymentInfo -Block $block
        $operation = "N/A"
        $depType = "N/A"
        $depName = $block.name
        $category = "unknown"
        
        if ($depInfo) {
            $operation = $depInfo.Operation
            $depType = $depInfo.Type
            $depName = $depInfo.Name
            $category = "operation"
        } else {
            $stateType = Get-StateBlockType -BlockName $block.name
            if ($stateType) {
                $depType = $stateType
                $category = "state"
            }
        }
        
        $ssloInventory += @{
            Block          = $block
            BlockName      = $block.name
            State          = $block.state
            Operation      = $operation
            DeploymentType = $depType
            DeploymentName = $depName
            Generation     = $block.generation
            Category       = $category
        }
    }
    
    # -------------------------------------------------------------------------
    # Step 3: Select blocks for backup
    # -------------------------------------------------------------------------
    $ssloBlocks = @()
    
    foreach ($item in $ssloInventory) {
        $block = $item.Block
        
        if ($item.Category -eq "operation") {
            if ($item.Operation -eq "CREATE" -and $item.State -eq "BOUND") {
                $ssloBlocks += @{
                    Block          = $block
                    DeploymentType = $item.DeploymentType
                    DeploymentName = $item.DeploymentName
                    Generation     = $item.Generation
                    BackupType     = $script:CAT_REPLAYABLE
                }
            }
        } elseif ($item.Category -eq "state") {
            if ($item.State -eq "UNBOUND") {
                $ssloBlocks += @{
                    Block          = $block
                    DeploymentType = $item.DeploymentType
                    DeploymentName = $item.DeploymentName
                    Generation     = $item.Generation
                    BackupType     = $script:CAT_STATE
                }
            }
        }
    }
    
    if ($ssloBlocks.Count -eq 0) {
        Write-LogWarn "No SSLO configuration blocks found."
        return $false
    }
    
    # -------------------------------------------------------------------------
    # Step 4: Deduplicate blocks by deployment name
    # -------------------------------------------------------------------------
    # Priority: replayable over state, highest generation within same type
    $dedupMap = @{}
    foreach ($item in $ssloBlocks) {
        $name = $item.DeploymentName
        $gen = 0
        try { $gen = [int]$item.Generation } catch { <# non-critical #> }
        
        if (-not $dedupMap.ContainsKey($name)) {
            $dedupMap[$name] = $item
        } else {
            $existing = $dedupMap[$name]
            if ($item.BackupType -eq $script:CAT_REPLAYABLE -and $existing.BackupType -eq $script:CAT_STATE) {
                $dedupMap[$name] = $item
            } elseif ($item.BackupType -eq $existing.BackupType) {
                $existingGen = 0
                try { $existingGen = [int]$existing.Generation } catch { <# non-critical #> }
                if ($gen -gt $existingGen) {
                    $dedupMap[$name] = $item
                }
            }
        }
    }
    
    $ssloBlocks = @($dedupMap.Values)
    Write-LogOk "$($ssloBlocks.Count) SSLO objects found"
    
    # -------------------------------------------------------------------------
    # Step 5: Sort by dependency order
    # -------------------------------------------------------------------------
    $sorted = @()
    foreach ($type in $script:DEPENDENCY_ORDER) {
        $matching = $ssloBlocks | Where-Object { $_.DeploymentType -eq $type }
        if ($matching) { $sorted += $matching }
    }
    $unknown = $ssloBlocks | Where-Object { $_.DeploymentType -notin $script:DEPENDENCY_ORDER }
    if ($unknown) { $sorted += $unknown }
    
    # -------------------------------------------------------------------------
    # Step 6: Strip runtime fields
    # -------------------------------------------------------------------------
    $outputBlocks = @()
    foreach ($item in $sorted) {
        $clean = Remove-RuntimeFields -Block $item.Block
        $outputBlocks += [ordered]@{
            deploymentType = $item.DeploymentType
            deploymentName = $item.DeploymentName
            backupType     = $item.BackupType
            block          = $clean
        }
    }
    
    # -------------------------------------------------------------------------
    # Step 7: Capture dependencies
    # -------------------------------------------------------------------------
    Write-Host ""
    $dependencies = Invoke-DependencyCapture -OutputBlocks $outputBlocks
    
    # -------------------------------------------------------------------------
    # Step 8: Write backup file
    # -------------------------------------------------------------------------
    $timestamp = Get-Date -Format "yyyyMMdd-HHmmss"
    $safeName = $script:RemoteHostname -replace '[^a-zA-Z0-9\-\.]', '_'
    $filename = "sslo-snapshot_${safeName}_${timestamp}.json"
    
    $backup = [ordered]@{
        metadata = [ordered]@{
            snapshotVersion  = $script:SNAPSHOT_FORMAT_VERSION
            tool             = "sslo-replay"
            toolVersion      = $script:TOOL_VERSION
            repository       = "https://github.com/hauptem/F5-SSL-Orchestrator-Tools"
            source           = [ordered]@{
                hostname     = $script:RemoteHostname
                tmosVersion  = $script:TMOSVersion
                ssloVersion  = $script:SSLOVersion
            }
            timestamp        = (Get-Date -Format "yyyy-MM-ddTHH:mm:ssZ")
            blockCount       = $outputBlocks.Count
        }
        blocks = $outputBlocks
    }
    
    # Ensure output directory exists
    if (-not (Test-Path $script:OUTPUT_DIR)) {
        New-Item -ItemType Directory -Path $script:OUTPUT_DIR -Force | Out-Null
    }
    
    $outputPath = Join-Path $script:OUTPUT_DIR $filename
    $backup | ConvertTo-Json -Depth 30 | Out-File -FilePath $outputPath -Encoding UTF8
    
    # Verify the written file round-trips. ConvertTo-Json silently truncates
    # beyond -Depth in PS 5.1, leaving parseable JSON with stringified objects
    $verifyOk = $true
    try {
        $verify = Get-Content -Path $outputPath -Raw -Encoding UTF8 | ConvertFrom-Json
        if (@($verify.blocks).Count -ne $outputBlocks.Count) { $verifyOk = $false }
        if ($verifyOk) {
            foreach ($vb in $verify.blocks) {
                $vprops = @($vb.block.inputProperties)
                if ($vprops.Count -eq 0 -or -not $vprops[0].id) { $verifyOk = $false; break }
            }
        }
    } catch { $verifyOk = $false }
    
    if (-not $verifyOk) {
        Write-Host ""
        Write-LogError "Snapshot verification failed - the written file did not round-trip cleanly."
        Write-LogError "Do not use this snapshot for replay: $outputPath"
        Press-EnterToContinue
        return $false
    }
    
    Write-Host ""
    Write-LogOk "Snapshot saved and verified: $outputPath"
    
    # Generate dependency manifest (text file, not used by the tool)
    $depFilePath = $null
    if ($dependencies -and $dependencies.objects -and $dependencies.objects.Count -gt 0) {
        $depFilename = "sslo-dependencies_${safeName}_${timestamp}.txt"
        $depFilePath = Join-Path $script:OUTPUT_DIR $depFilename
        
        $depGroups = [ordered]@{
            "CERTIFICATES AND KEYS" = @("certificate", "key", "ca_bundle")
            "VLANS"                 = @("vlan")
            "MONITORS"              = @("monitor_tcp", "monitor_http", "monitor_https", "monitor_icmp", "monitor_unknown")
            "PROFILES"              = @("profile_tcp", "profile_http")
            "CIPHER CONFIGURATION"  = @("cipher_rule", "cipher_group")
            "LOG PUBLISHERS"        = @("log_publisher")
            "NETWORK OBJECTS"       = @("snatpool", "gateway_pool")
            "IRULES"                = @("irule")
            "POLICIES"              = @("ltm_policy", "access_profile")
            "DATAGROUPS"            = @("datagroup")
            "URL CATEGORIES"        = @("url_category")
        }
        
        $lines = @()
        $lines += "SSLO-Replay Dependency Manifest"
        $lines += "=" * 72
        $lines += "Source:   $($script:RemoteHostname) (TMOS $($script:TMOSVersion), SSLO $($script:SSLOVersion))"
        $lines += "Date:     $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')"
        $lines += "Snapshot: $filename"
        $lines += ""
        $lines += "These objects must exist on the target device before replay."
        $lines += "Configs are included below for reference when recreating."
        $lines += ""
        
        foreach ($groupName in $depGroups.Keys) {
            $groupTypes = $depGroups[$groupName]
            $groupDeps = @($dependencies.objects | Where-Object { $_.type -in $groupTypes })
            if ($groupDeps.Count -eq 0) { continue }
            
            $lines += "=" * 72
            $lines += $groupName
            $lines += "=" * 72
            $lines += ""
            
            foreach ($dep in $groupDeps) {
                $displayType = Get-DepDisplayType $dep.type
                $refs = ($dep.referencedBy -join ", ")
                $lines += "  ${displayType}: $($dep.path)"
                $lines += "  Referenced by: $refs"
                
                if ($dep.config) {
                    $lines += ""
                    
                    # Type-specific readable output
                    if ($dep.type -eq "datagroup") {
                        $lines += "  Type: $($dep.config.type)"
                        if ($dep.config.records) {
                            $lines += "  Records:"
                            foreach ($rec in $dep.config.records) {
                                $entry = "    $($rec.name)"
                                if ($rec.data) { $entry += " := $($rec.data)" }
                                $lines += $entry
                            }
                        }
                    } elseif ($dep.type -eq "url_category") {
                        $configJson = $dep.config | ConvertTo-Json -Depth 10
                        foreach ($cl in ($configJson -split "`n")) {
                            $lines += "    $cl"
                        }
                    } elseif ($dep.type -eq "vlan" -and $dep.config.tag) {
                        $lines += "  VLAN tag: $($dep.config.tag)"
                        if ($dep.config.interfaces) {
                            $lines += "  Interfaces: $(($dep.config.interfaces | ForEach-Object { $_.name }) -join ', ')"
                        }
                    } elseif ($dep.type -match "^monitor_") {
                        if ($dep.config.interval) { $lines += "  Interval: $($dep.config.interval)" }
                        if ($dep.config.timeout) { $lines += "  Timeout: $($dep.config.timeout)" }
                        if ($dep.config.send) { $lines += "  Send: $($dep.config.send)" }
                        if ($dep.config.recv) { $lines += "  Recv: $($dep.config.recv)" }
                        if ($dep.config.destination) { $lines += "  Destination: $($dep.config.destination)" }
                    } else {
                        # Generic: dump as indented JSON
                        $configJson = $dep.config | ConvertTo-Json -Depth 10
                        foreach ($cl in ($configJson -split "`n")) {
                            $lines += "    $cl"
                        }
                    }
                }
                
                $lines += ""
            }
        }
        
        $lines -join "`r`n" | Out-File -FilePath $depFilePath -Encoding UTF8
        Write-LogOk "Dependencies saved to: $depFilePath"
    }
    
    Start-Sleep -Seconds 2
    Clear-Host
    Write-LogSection "Snapshot Summary"
    
    # SSLO objects
    $lastType = ""
    foreach ($item in $outputBlocks) {
        if ($item.deploymentType -ne $lastType) {
            $lastType = $item.deploymentType
            Write-Host ""
            Write-Host "  $(Get-TypeDisplayName $lastType)" -ForegroundColor White
        }
        Write-Host "    $($item.deploymentName)" -ForegroundColor White
    }
    
    # Dependencies
    if ($dependencies -and $dependencies.objects -and $dependencies.objects.Count -gt 0) {
        Write-Host ""
        Write-Host "  Dependencies ($($dependencies.objects.Count))" -ForegroundColor White
        
        $depByType = @{}
        foreach ($dep in $dependencies.objects) {
            if (-not $depByType.ContainsKey($dep.type)) { $depByType[$dep.type] = @() }
            $depByType[$dep.type] += $dep
        }
        
        foreach ($depType in @($depByType.Keys | Sort-Object)) {
            $depLabel = Get-DepDisplayType $depType
            foreach ($dep in ($depByType[$depType] | Sort-Object { $_.path })) {
                Write-Host "    $depLabel $($dep.path)" -ForegroundColor DarkGray
            }
        }
    }
    
    Write-Host ""
    Write-LogOk "$($outputBlocks.Count) objects recorded"
    Write-LogOk "Snapshot: $outputPath"
    if ($depFilePath) {
        Write-LogOk "Dependencies: $depFilePath"
    }
    Write-Host ""
    
    Press-EnterToContinue
    return $true
}


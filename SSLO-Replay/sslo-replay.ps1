# Resolve the dependency tree for a security policy from a snapshot
# Returns: ordered list of block entries (services -> chains -> the policy)
function Get-PolicyDependencyTree {
    param(
        [object]$Backup,
        [string]$PolicyName
    )
    
    $requiredNames = [ordered]@{}
    
    # Find the policy block
    $policyBlock = $Backup.blocks | Where-Object { $_.deploymentName -eq $PolicyName -and $_.deploymentType -eq "SECURITY_POLICY" } | Select-Object -First 1
    if (-not $policyBlock) { return @() }
    
    $requiredNames[$PolicyName] = $true
    
    # Extract service chain references from policy rules
    $policyDataId = $script:TYPE_PROPERTY_MAP["SECURITY_POLICY"]
    foreach ($prop in $policyBlock.block.inputProperties) {
        if ($prop.id -eq $policyDataId) {
            $policyConfig = $prop.value
            # Walk rules[].actionOptions.serviceChain (from bigip_sslo_config_policy.py)
            if ($policyConfig.rules) {
                foreach ($rule in $policyConfig.rules) {
                    if ($rule.actionOptions -and $rule.actionOptions.serviceChain -and $rule.actionOptions.serviceChain -ne "") {
                        $requiredNames[$rule.actionOptions.serviceChain] = $true
                    }
                }
            }
            # Default action service chain
            if ($policyConfig.defaultActionOptions -and $policyConfig.defaultActionOptions.serviceChain -and $policyConfig.defaultActionOptions.serviceChain -ne "") {
                $requiredNames[$policyConfig.defaultActionOptions.serviceChain] = $true
            }
            break
        }
    }
    
    # Walk each chain's orderedServiceList[].name (from bigip_sslo_config_service_chain.py)
    foreach ($chainName in @($requiredNames.Keys)) {
        $chainBlock = $Backup.blocks | Where-Object { $_.deploymentName -eq $chainName -and $_.deploymentType -eq "SERVICE_CHAIN" } | Select-Object -First 1
        if ($chainBlock) {
            $chainDataId = $script:TYPE_PROPERTY_MAP["SERVICE_CHAIN"]
            foreach ($prop in $chainBlock.block.inputProperties) {
                if ($prop.id -eq $chainDataId) {
                    if ($prop.value.orderedServiceList) {
                        foreach ($svc in $prop.value.orderedServiceList) {
                            if ($svc.name) { $requiredNames[$svc.name] = $true }
                        }
                    }
                    break
                }
            }
        }
    }
    
    # Return snapshot blocks in dependency order
    $result = @()
    foreach ($type in $script:DEPENDENCY_ORDER) {
        foreach ($name in $requiredNames.Keys) {
            $block = $Backup.blocks | Where-Object { $_.deploymentName -eq $name -and $_.deploymentType -eq $type } | Select-Object -First 1
            if ($block) { $result += $block }
        }
    }
    return $result
}

# Build a topology MODIFY block from the target's current state with updated policy
# Source: Ansible sslo_config_topology.py add_json_metadata (lines 1651-1653),
#         sslo_templates/sslo_config_topology.py create_modify template
function Convert-StateBlockToModify {
    param(
        [object]$TargetStateBlock,
        [string]$TopologyName,
        [string]$BlockId,
        [string]$NewPolicyName
    )
    
    $dataPropertyId = $script:TYPE_PROPERTY_MAP["TOPOLOGY"]
    $typeProperties = $script:TYPE_INPUT_PROPERTIES["TOPOLOGY"]
    
    # Extract topology config data from the target's state block
    $configData = $null
    foreach ($prop in $TargetStateBlock.inputProperties) {
        if ($prop.id -eq $dataPropertyId) {
            $configData = $prop.value
            break
        }
    }
    
    if ($null -eq $configData) {
        Write-LogError "No topology config data found for $TopologyName"
        return $null
    }
    
    # Strip generated fields and update the policy reference
    $configData = Remove-GeneratedFields -ConfigData $configData
    $configData.securityPolicyReference = $NewPolicyName
    
    # Add existingBlockId for MODIFY (from Ansible template line 165-166)
    if ($configData -is [PSCustomObject]) {
        if ($configData.PSObject.Properties["existingBlockId"]) {
            $configData.existingBlockId = $BlockId
        } else {
            $configData | Add-Member -NotePropertyName "existingBlockId" -NotePropertyValue $BlockId
        }
    }
    
    # Build inputProperties: MODIFY operation context + type-specific properties
    $inputProps = @()
    
    # Operation context - MODIFY with deployment reference to existing block
    $inputProps += @{
        id = "f5-ssl-orchestrator-operation-context"
        type = "JSON"
        value = [ordered]@{
            operationType = "MODIFY"
            deploymentType = "TOPOLOGY"
            deploymentName = $TopologyName
            deploymentReference = "https://localhost/mgmt/shared/iapp/blocks/$BlockId"
            partition = "Common"
            strictness = $false
        }
    }
    
    # Type-specific properties - same as CREATE, with dependent configs from target
    foreach ($propDef in $typeProperties) {
        if ($propDef.id -eq $dataPropertyId) {
            $inputProps += @{
                id = $propDef.id
                type = "JSON"
                value = $configData
            }
        } else {
            $depValue = $null
            
            switch ($propDef.id) {
                "f5-ssl-orchestrator-general-settings" {
                    $gsBlockResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks?`$filter=name+eq+'ssloGS_global'"
                    if ($gsBlockResult.Success -and $gsBlockResult.Response.items.Count -gt 0) {
                        $depValue = $gsBlockResult.Response.items[0].inputProperties[0].value
                    }
                }
                "f5-ssl-orchestrator-policy" {
                    # Fetch the NEW policy's config from target (just created)
                    $policyResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks?`$filter=name+eq+'$NewPolicyName'"
                    if ($policyResult.Success -and $policyResult.Response.items.Count -gt 0) {
                        foreach ($pp in $policyResult.Response.items[0].inputProperties) {
                            if ($pp.id -eq "f5-ssl-orchestrator-policy") {
                                $depValue = $pp.value
                                break
                            }
                        }
                    }
                }
                "f5-ssl-orchestrator-tls" {
                    $sslRefs = $configData.sslSettingReference
                    if ($sslRefs -and $sslRefs.Count -gt 0) {
                        $sslName = if ($sslRefs -is [System.Array]) { $sslRefs[0] } else { $sslRefs }
                        $sslResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks?`$filter=name+eq+'$sslName'"
                        if ($sslResult.Success -and $sslResult.Response.items.Count -gt 0) {
                            foreach ($sp in $sslResult.Response.items[0].inputProperties) {
                                if ($sp.id -eq "f5-ssl-orchestrator-tls") {
                                    $depValue = $sp.value
                                    break
                                }
                            }
                        }
                    }
                }
            }
            
            $inputProps += @{
                id = $propDef.id
                type = "JSON"
                value = if ($null -ne $depValue) { $depValue } else { $propDef.empty }
            }
        }
    }
    
    # Build the MODIFY block
    # Top-level operation stays "CREATE" per Ansible template (gc processor uses operationType)
    $modifyBlock = [ordered]@{
        name = "sslo_ob_TOPOLOGY_MODIFY_$TopologyName"
        inputProperties = $inputProps
        configurationProcessorReference = @{
            link = "https://localhost/mgmt/shared/iapp/processors/f5-iappslx-ssl-orchestrator-gc"
        }
        configProcessorTimeoutSeconds = $script:GC_PROCESSOR_TIMEOUT
        statsProcessorTimeoutSeconds = 60
        configProcessorAffinity = @{
            processorPolicy = "LOCAL"
            affinityProcessorReference = @{
                link = "https://localhost/mgmt/shared/iapp/affinity/local"
            }
        }
        state = "BINDING"
        presentationHtmlReference = @{
            link = "https://localhost/iapps/f5-iappslx-ssl-orchestrator/sgc/sgcIndex.html"
        }
        operation = "CREATE"
    }
    
    return $modifyBlock
}

# POST a block to the gc processor and poll for BOUND state
function Invoke-BlockPost {
    param(
        [object]$Block,
        [string]$Label,
        [int]$PollMax = 120
    )
    
    $postBody = $Block | ConvertTo-Json -Depth 30
    $result = Invoke-F5Post -Endpoint "/mgmt/shared/iapp/blocks" -Body $postBody
    
    if (-not $result.Success) {
        Write-LogError "$Label failed (HTTP $($result.StatusCode))"
        return $false
    }
    
    $blockId = $null
    try { $blockId = $result.Response.id } catch { <# non-critical #> }
    
    if ($blockId) {
        $pollElapsed = 0
        $finalState = "UNKNOWN"
        
        while ($pollElapsed -lt $PollMax) {
            Start-Sleep -Seconds 5
            $pollElapsed += 5
            
            $pollResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks/$blockId"
            if ($pollResult.Success) {
                $finalState = $pollResult.Response.state
                if ($finalState -in @("BOUND", "ERROR")) { break }
            }
            
            Write-Host "`r  Waiting for $Label...    " -NoNewline
        }
        Write-Host ""
        
        if ($finalState -eq "BOUND") {
            Write-LogOk "$Label"
            return $true
        } elseif ($finalState -eq "ERROR") {
            Write-LogError "$Label failed (ERROR state)"
            $errDetail = ""
            try { $errDetail = [string]$pollResult.Response.error } catch { <# non-critical #> }
            if ($errDetail) {
                if ($errDetail.Length -gt 300) { $errDetail = $errDetail.Substring(0, 300) + "..." }
                Write-LogError "  $errDetail"
            }
            return $false
        } else {
            Write-LogWarn "$Label timed out (state: $finalState)"
            return $false
        }
    }
    
    Write-LogWarn "$Label - posted but no block ID returned; state not verified"
    return $true
}

# Apply a security policy from a snapshot to an existing topology on the target
function Invoke-PolicySwap {
    param([object]$Backup)
    
    # -------------------------------------------------------------------------
    # Select policy from snapshot
    # -------------------------------------------------------------------------
    $policies = @($Backup.blocks | Where-Object { $_.deploymentType -eq "SECURITY_POLICY" })
    
    Clear-Host
    Write-LogSection "Select Security Policy"
    Write-Host ""
    Write-Host "  Policies in snapshot:" -ForegroundColor White
    Write-Host ""
    
    for ($i = 0; $i -lt $policies.Count; $i++) {
        $num = $i + 1
        Write-Host "     " -NoNewline
        Write-Host "$num" -NoNewline -ForegroundColor Yellow; Write-Host ")" -NoNewline -ForegroundColor White
        Write-Host "  $($policies[$i].deploymentName)" -ForegroundColor White
    }
    
    Write-Host ""
    $rangeLabel = if ($policies.Count -eq 1) { "[1]" } else { "[1-$($policies.Count)]" }
    $polChoice = Read-Host "  Select $rangeLabel (0 to cancel)"
    
    if ($polChoice -eq "0") { return }
    
    $polIdx = 0
    if (-not ([int]::TryParse($polChoice, [ref]$polIdx)) -or $polIdx -lt 1 -or $polIdx -gt $policies.Count) {
        Write-LogWarn "Invalid selection."
        Press-EnterToContinue
        return
    }
    
    $selectedPolicy = $policies[$polIdx - 1]
    $policyName = $selectedPolicy.deploymentName
    
    # -------------------------------------------------------------------------
    # Query target for existing topologies
    # -------------------------------------------------------------------------
    Clear-Host
    Write-LogSection "Select Target Topology"
    Write-Host ""
    Write-LogStep "Querying target for existing topologies..."
    
    $targetTopos = @()
    $blocksResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks"
    if ($blocksResult.Success -and $blocksResult.Response.items) {
        foreach ($block in $blocksResult.Response.items) {
            if ($block.state -ne "UNBOUND") { continue }
            $stateType = Get-StateBlockType -BlockName $block.name
            if ($stateType -eq "TOPOLOGY") {
                # Extract current policy reference for display
                $currentPolicy = ""
                foreach ($prop in $block.inputProperties) {
                    if ($prop.id -eq "f5-ssl-orchestrator-topology") {
                        if ($prop.value.securityPolicyReference) {
                            $currentPolicy = $prop.value.securityPolicyReference
                        }
                        break
                    }
                }
                $targetTopos += @{
                    Name          = $block.name
                    BlockId       = $block.id
                    Block         = $block
                    CurrentPolicy = $currentPolicy
                }
            }
        }
    }
    
    if ($targetTopos.Count -eq 0) {
        Write-LogWarn "No existing topologies found on target."
        Press-EnterToContinue
        return
    }
    
    Write-Host ""
    Write-Host "  Topologies on target:" -ForegroundColor White
    Write-Host ""
    
    for ($i = 0; $i -lt $targetTopos.Count; $i++) {
        $num = $i + 1
        $topo = $targetTopos[$i]
        Write-Host "     " -NoNewline
        Write-Host "$num" -NoNewline -ForegroundColor Yellow; Write-Host ")" -NoNewline -ForegroundColor White
        Write-Host "  $($topo.Name)" -NoNewline -ForegroundColor White
        if ($topo.CurrentPolicy) {
            Write-Host "      policy: $($topo.CurrentPolicy)" -ForegroundColor DarkGray
        } else {
            Write-Host "" -ForegroundColor White
        }
    }
    
    Write-Host ""
    $rangeLabel = if ($targetTopos.Count -eq 1) { "[1]" } else { "[1-$($targetTopos.Count)]" }
    $topoChoice = Read-Host "  Select $rangeLabel (0 to cancel)"
    
    if ($topoChoice -eq "0") { return }
    
    $topoIdx = 0
    if (-not ([int]::TryParse($topoChoice, [ref]$topoIdx)) -or $topoIdx -lt 1 -or $topoIdx -gt $targetTopos.Count) {
        Write-LogWarn "Invalid selection."
        Press-EnterToContinue
        return
    }
    
    $selectedTopo = $targetTopos[$topoIdx - 1]
    
    # -------------------------------------------------------------------------
    # Name the policy on the target
    # -------------------------------------------------------------------------
    # Derive default name from target topology: sslo_X -> ssloP_X
    $defaultPolicyName = $selectedTopo.Name -replace "^sslo_", "ssloP_"
    
    Write-Host ""
    $newPolicyName = Read-Host "  Policy name on target [$defaultPolicyName]"
    if ([string]::IsNullOrWhiteSpace($newPolicyName)) {
        $newPolicyName = $defaultPolicyName
    }
    # Ensure ssloP_ prefix
    if ($newPolicyName -notmatch "^ssloP_") {
        $newPolicyName = "ssloP_$newPolicyName"
    }
    
    # -------------------------------------------------------------------------
    # Confirm
    # -------------------------------------------------------------------------
    Write-Host ""
    Write-Host "  Apply " -NoNewline -ForegroundColor White
    Write-Host $newPolicyName -NoNewline -ForegroundColor Yellow
    Write-Host " to " -NoNewline -ForegroundColor White
    Write-Host "$($selectedTopo.Name)" -NoNewline -ForegroundColor Yellow
    Write-Host "?" -ForegroundColor White
    if ($policyName -ne $newPolicyName) {
        Write-Host "  Source policy: $policyName" -ForegroundColor DarkGray
    }
    if ($selectedTopo.CurrentPolicy) {
        Write-Host "  Replaces: $($selectedTopo.CurrentPolicy)" -ForegroundColor DarkGray
    }
    $confirm = Read-Host "  Proceed? [y/N]"
    if ($confirm -notin @("y", "Y", "yes", "YES")) { return }
    
    # -------------------------------------------------------------------------
    # Pre-flight: Validate all dependencies before touching anything
    # -------------------------------------------------------------------------
    Clear-Host
    Write-LogSection "Policy Swap Plan"
    Write-Host ""
    
    $depTree = Get-PolicyDependencyTree -Backup $Backup -PolicyName $policyName
    $planItems = @()
    $blockers = @()
    
    foreach ($entry in $depTree) {
        $typeLabel = Get-TypeLabel $entry.deploymentType
        $depName = $entry.deploymentName
        $createName = $depName
        
        if ($entry.deploymentType -eq "SECURITY_POLICY" -and $depName -eq $policyName) {
            $createName = $newPolicyName
        }
        
        # Check if exists on target
        $existingBlockId = $null
        $existsResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks?`$filter=name+eq+'$createName'"
        if ($existsResult.Success -and $existsResult.Response.items -and $existsResult.Response.items.Count -gt 0) {
            $existingBlockId = $existsResult.Response.items[0].id
        }
        
        if ($existingBlockId) {
            if ($entry.deploymentType -eq "SECURITY_POLICY" -and $createName -eq $newPolicyName) {
                # Policy exists - will be overwritten
                Write-Host "  [WARN]" -NoNewline -ForegroundColor Yellow
                Write-Host "  $typeLabel $createName" -NoNewline -ForegroundColor White
                Write-Host " (exists — will be overwritten)" -ForegroundColor Yellow
                $planItems += @{ Entry = $entry; CreateName = $createName; Action = "modify"; BlockId = $existingBlockId }
            } else {
                Write-Host "  [ OK ]" -NoNewline -ForegroundColor Green
                Write-Host "  $typeLabel $createName" -NoNewline -ForegroundColor White
                Write-Host " (exists)" -ForegroundColor DarkGray
            }
        } else {
            # Missing - can we create it?
            if ($entry.deploymentType -eq "SERVICE") {
                Write-Host "  [FAIL]" -NoNewline -ForegroundColor Red
                Write-Host "  $typeLabel $createName" -NoNewline -ForegroundColor White
                Write-Host " - does not exist on target" -ForegroundColor Red
                $blockers += "$typeLabel $createName — create on target first or use a full replay"
            } elseif ($entry.deploymentType -eq "SSL_SETTINGS") {
                Write-Host "  [FAIL]" -NoNewline -ForegroundColor Red
                Write-Host "  $typeLabel $createName" -NoNewline -ForegroundColor White
                Write-Host " - does not exist on target" -ForegroundColor Red
                $blockers += "$typeLabel $createName — create on target first or use a full replay"
            } else {
                Write-Host "  [WARN]" -NoNewline -ForegroundColor Yellow
                Write-Host "  $typeLabel $createName" -NoNewline -ForegroundColor White
                Write-Host " (will be created)" -ForegroundColor Yellow
                $planItems += @{ Entry = $entry; CreateName = $createName; Action = "create"; BlockId = $null }
            }
        }
    }
    
    # Topology MODIFY
    Write-Host ""
    Write-Host "  [WARN]" -NoNewline -ForegroundColor Yellow
    Write-Host "  topology $($selectedTopo.Name)" -NoNewline -ForegroundColor White
    Write-Host " (will be modified)" -ForegroundColor Yellow
    
    # Stop on blockers
    if ($blockers.Count -gt 0) {
        Write-Host ""
        Write-LogError "$($blockers.Count) issue(s) prevent this operation:"
        foreach ($b in $blockers) {
            Write-Host "    $b" -ForegroundColor Red
        }
        Write-Host ""
        Press-EnterToContinue
        return
    }
    
    # Confirm execution
    Write-Host ""
    $proceed = Read-Host "  Proceed with this plan? [y/N]"
    if ($proceed -notin @("y", "Y", "yes", "YES")) { return }
    
    # -------------------------------------------------------------------------
    # Execute: Create and modify per the plan
    # -------------------------------------------------------------------------
    Clear-Host
    Write-LogSection "Modifying Policy"
    Write-Host ""
    
    $depFailed = $false
    $completedActions = @()
    
    foreach ($item in $planItems) {
        $entry = $item.Entry
        $createName = $item.CreateName
        $typeLabel = Get-TypeLabel $entry.deploymentType
        
        # For the renamed policy, update the name field inside the config data
        $stateBlock = $entry.block
        if ($entry.deploymentType -eq "SECURITY_POLICY" -and $entry.deploymentName -eq $policyName -and $createName -ne $entry.deploymentName) {
            $stateBlock = $entry.block | ConvertTo-Json -Depth 30 | ConvertFrom-Json
            foreach ($prop in $stateBlock.inputProperties) {
                if ($prop.id -eq "f5-ssl-orchestrator-policy" -and $prop.value.name) {
                    $prop.value.name = $createName
                }
            }
        }
        
        if ($item.Action -eq "modify") {
            # MODIFY existing policy - overwrite with snapshot config
            Write-LogStep "Updating $typeLabel $createName..."
            $postBlock = Convert-StateBlockToCreate `
                -StateBlock $stateBlock `
                -DeploymentType $entry.deploymentType `
                -DeploymentName $createName
            
            if (-not $postBlock) {
                Write-LogError "$createName - could not prepare block"
                $depFailed = $true
                break
            }
            
            # Patch CREATE block to MODIFY
            $postBlock.name = "sslo_ob_SECURITY_POLICY_MODIFY_$createName"
            foreach ($prop in $postBlock.inputProperties) {
                if ($prop.id -eq "f5-ssl-orchestrator-operation-context") {
                    $prop.value.operationType = "MODIFY"
                    $prop.value.deploymentReference = "https://localhost/mgmt/shared/iapp/blocks/$($item.BlockId)"
                }
                if ($prop.id -eq "f5-ssl-orchestrator-policy") {
                    if ($prop.value -is [PSCustomObject]) {
                        if ($prop.value.PSObject.Properties["existingBlockId"]) {
                            $prop.value.existingBlockId = $item.BlockId
                        } else {
                            $prop.value | Add-Member -NotePropertyName "existingBlockId" -NotePropertyValue $item.BlockId
                        }
                    }
                }
            }
        } else {
            # CREATE new object
            Write-LogStep "Creating $typeLabel $createName..."
            $postBlock = Convert-StateBlockToCreate `
                -StateBlock $stateBlock `
                -DeploymentType $entry.deploymentType `
                -DeploymentName $createName
            
            if (-not $postBlock) {
                Write-LogError "$createName - could not prepare block"
                $depFailed = $true
                break
            }
        }
        
        $pollMax = if ($entry.deploymentType -eq "TOPOLOGY") { 180 } else { 120 }
        if (-not (Invoke-BlockPost -Block $postBlock -Label "$typeLabel $createName" -PollMax $pollMax)) {
            $depFailed = $true
            break
        }
        
        if ($item.Action -eq "modify") {
            $completedActions += "$typeLabel $createName modified - new content is in effect"
        } else {
            $completedActions += "$typeLabel $createName created"
        }
        
        Start-Sleep -Seconds 5
    }
    
    if ($depFailed) {
        Write-Host ""
        Write-LogError "Operation failed. Review the error above."
        if ($completedActions.Count -gt 0) {
            Write-Host ""
            Write-LogWarn "Completed before the failure - these changes are live on the target:"
            foreach ($a in $completedActions) { Write-Host "    $a" -ForegroundColor Yellow }
        }
        Press-EnterToContinue
        return
    }
    
    # -------------------------------------------------------------------------
    # MODIFY the topology
    # -------------------------------------------------------------------------
    Write-Host ""
    Write-LogSection "Applying Policy"
    Write-Host ""
    
    Write-LogStep "Modifying topology $($selectedTopo.Name)..."
    $modifyBlock = Convert-StateBlockToModify `
        -TargetStateBlock $selectedTopo.Block `
        -TopologyName $selectedTopo.Name `
        -BlockId $selectedTopo.BlockId `
        -NewPolicyName $newPolicyName
    
    if (-not $modifyBlock) {
        Write-LogError "Could not prepare MODIFY block."
        Press-EnterToContinue
        return
    }
    
    if (-not (Invoke-BlockPost -Block $modifyBlock -Label "topology $($selectedTopo.Name)" -PollMax 180)) {
        Write-LogError "Topology modification failed."
        if ($completedActions.Count -gt 0) {
            Write-Host ""
            Write-LogWarn "Completed before the failure - these changes are live on the target:"
            foreach ($a in $completedActions) { Write-Host "    $a" -ForegroundColor Yellow }
        }
        Press-EnterToContinue
        return
    }
    
    # -------------------------------------------------------------------------
    # Save configuration
    # -------------------------------------------------------------------------
    Write-Host ""
    Write-LogStep "Saving BIG-IP configuration..."
    if (Save-F5Config) {
        Write-LogOk "Configuration saved"
    } else {
        Write-LogWarn "Could not save configuration. Save manually via BIG-IP GUI or tmsh."
    }
    
    # -------------------------------------------------------------------------
    # Summary
    # -------------------------------------------------------------------------
    Write-Host ""
    Write-LogOk "Policy $newPolicyName applied to $($selectedTopo.Name)"
    Write-Host ""
    
    Press-EnterToContinue
}

# Option 2: Replay a snapshot to the connected target
function Invoke-SsloRestore {
    # -------------------------------------------------------------------------
    # Step 1: Select and import backup file
    # -------------------------------------------------------------------------
    $filePath = Select-BackupFile
    if (-not $filePath) { return }
    
    Write-Host ""
    $backup = Import-SsloBackup -FilePath $filePath
    if (-not $backup) {
        Press-EnterToContinue
        return
    }
    
    # -------------------------------------------------------------------------
    # Step 2: Display backup metadata
    # -------------------------------------------------------------------------
    Clear-Host
    Write-LogSection "Snapshot Details"
    Write-Host ""
    Write-LogInfo "Source host:     $($backup.metadata.source.hostname)"
    Write-LogInfo "TMOS version:    $($backup.metadata.source.tmosVersion)"
    Write-LogInfo "SSLO version:    $($backup.metadata.source.ssloVersion)"
    Write-LogInfo "Snapshot date:   $($backup.metadata.timestamp)"
    Write-LogInfo "Block count:     $($backup.metadata.blockCount)"
    
    # -------------------------------------------------------------------------
    # Step 3: Target compatibility check
    # -------------------------------------------------------------------------
    Write-Host ""
    $mismatches = @()
    
    if ($backup.metadata.source.hostname -ne $script:RemoteHostname) {
        $mismatches += @{ Label = "Hostname"; Backup = $backup.metadata.source.hostname; Target = $script:RemoteHostname }
    }
    if ($backup.metadata.source.tmosVersion -ne $script:TMOSVersion) {
        $mismatches += @{ Label = "TMOS"; Backup = $backup.metadata.source.tmosVersion; Target = $script:TMOSVersion }
    }
    if ($backup.metadata.source.ssloVersion -and $backup.metadata.source.ssloVersion -ne $script:SSLOVersion) {
        $mismatches += @{ Label = "SSLO"; Backup = $backup.metadata.source.ssloVersion; Target = $script:SSLOVersion }
    }
    
    if ($mismatches.Count -gt 0) {
        foreach ($m in $mismatches) {
            Write-LogWarn "$($m.Label) differs - snapshot: $($m.Backup) / target: $($m.Target)"
        }
        Write-Host ""
        $confirm = Read-Host "  Proceed with replay to $($script:RemoteHostname)? [y/N]"
        if ($confirm -notin @("y", "Y", "yes", "YES")) {
            Write-LogInfo "Replay cancelled."
            Press-EnterToContinue
            return
        }
    } else {
        Write-LogOk "Target matches snapshot source"
    }
    
    # -------------------------------------------------------------------------
    # Step 4: Replay scope selection
    # -------------------------------------------------------------------------
    $topologies = @($backup.blocks | Where-Object { $_.deploymentType -eq "TOPOLOGY" })
    $policies = @($backup.blocks | Where-Object { $_.deploymentType -eq "SECURITY_POLICY" })
    
    # Show scope menu when there are multiple topologies or policies available for swap
    if ($topologies.Count -gt 1 -or $policies.Count -gt 0) {
        Write-Host ""
        Write-Host "     " -NoNewline
        Write-Host "1" -NoNewline -ForegroundColor Yellow; Write-Host ")" -NoNewline -ForegroundColor White
        Write-Host "  Replay entire snapshot" -ForegroundColor White
        if ($topologies.Count -gt 0) {
            Write-Host "     " -NoNewline
            Write-Host "2" -NoNewline -ForegroundColor Yellow; Write-Host ")" -NoNewline -ForegroundColor White
            Write-Host "  Replay a single topology" -ForegroundColor White
        }
        if ($policies.Count -gt 0) {
            Write-Host "     " -NoNewline
            Write-Host "3" -NoNewline -ForegroundColor Yellow; Write-Host ")" -NoNewline -ForegroundColor White
            Write-Host "  Apply a security policy to an existing topology" -ForegroundColor White
        }
        Write-Host ""
        $scopeChoice = Read-Host "  Select (0 to cancel)"
        
        if ($scopeChoice -eq "0") { return }
        
        # Policy swap - branches to its own flow and returns
        if ($scopeChoice -eq "3" -and $policies.Count -gt 0) {
            Invoke-PolicySwap -Backup $backup
            return
        }
        
        # Single topology scope
        if ($scopeChoice -eq "2" -and $topologies.Count -gt 0) {
            Write-Host ""
            for ($i = 0; $i -lt $topologies.Count; $i++) {
                $num = $i + 1
                Write-Host "     " -NoNewline
                Write-Host "$num" -NoNewline -ForegroundColor Yellow; Write-Host ")" -NoNewline -ForegroundColor White
                Write-Host "  $($topologies[$i].deploymentName)" -ForegroundColor White
            }
            Write-Host ""
            $rangeLabel = if ($topologies.Count -eq 1) { "[1]" } else { "[1-$($topologies.Count)]" }
            $topoChoice = Read-Host "  Select $rangeLabel"
            
            $topoIdx = 0
            if ([int]::TryParse($topoChoice, [ref]$topoIdx) -and $topoIdx -ge 1 -and $topoIdx -le $topologies.Count) {
                $selectedTopo = $topologies[$topoIdx - 1]
                
                # Resolve dependency tree from the selected topology
                $requiredNames = @{}
                $requiredNames[$selectedTopo.deploymentName] = $true
                
                # Get topology config data
                $topoDataId = $script:TYPE_PROPERTY_MAP["TOPOLOGY"]
                $topoConfig = $null
                foreach ($prop in $selectedTopo.block.inputProperties) {
                    if ($prop.id -eq $topoDataId) { $topoConfig = $prop.value; break }
                }
                
                if ($topoConfig) {
                    # SSL settings
                    if ($topoConfig.sslSettingReference) {
                        $refs = $topoConfig.sslSettingReference
                        if ($refs -is [System.Array]) { foreach ($r in $refs) { $requiredNames[$r] = $true } }
                        else { $requiredNames[$refs] = $true }
                    }
                    
                    # Security policy and its dependency tree
                    if ($topoConfig.securityPolicyReference) {
                        $policyName = $topoConfig.securityPolicyReference
                        $requiredNames[$policyName] = $true
                        
                        $policyBlock = $backup.blocks | Where-Object { $_.deploymentName -eq $policyName } | Select-Object -First 1
                        if ($policyBlock) {
                            $policyDataId = $script:TYPE_PROPERTY_MAP["SECURITY_POLICY"]
                            foreach ($prop in $policyBlock.block.inputProperties) {
                                if ($prop.id -eq $policyDataId) {
                                    $policyConfig = $prop.value
                                    if ($policyConfig.rules) {
                                        foreach ($rule in $policyConfig.rules) {
                                            if ($rule.actionOptions -and $rule.actionOptions.serviceChain) {
                                                $chainName = $rule.actionOptions.serviceChain
                                                if ($chainName -ne "") {
                                                    $requiredNames[$chainName] = $true
                                                }
                                            }
                                        }
                                    }
                                    break
                                }
                            }
                        }
                        
                        foreach ($chainName in @($requiredNames.Keys)) {
                            $chainBlock = $backup.blocks | Where-Object { $_.deploymentName -eq $chainName -and $_.deploymentType -eq "SERVICE_CHAIN" } | Select-Object -First 1
                            if ($chainBlock) {
                                $chainDataId = $script:TYPE_PROPERTY_MAP["SERVICE_CHAIN"]
                                foreach ($prop in $chainBlock.block.inputProperties) {
                                    if ($prop.id -eq $chainDataId) {
                                        $chainConfig = $prop.value
                                        if ($chainConfig.orderedServiceList) {
                                            foreach ($svc in $chainConfig.orderedServiceList) {
                                                if ($svc.name) { $requiredNames[$svc.name] = $true }
                                            }
                                        }
                                        break
                                    }
                                }
                            }
                        }
                    }
                }
                
                $backup.blocks = @($backup.blocks | Where-Object { $requiredNames.ContainsKey($_.deploymentName) })
                Write-LogOk "Scoped to topology $($selectedTopo.deploymentName) ($($backup.blocks.Count) objects)"
                
                # ---------------------------------------------------------
                # Dynamic naming (optional): rename the topology stack to a
                # new base name. Renaming applies ONLY to the topology, its
                # SSL settings, and its security policy. Services, service
                # chains, and /Common/ dependencies are never renamed - they
                # are outside replay's naming scope by design
                # ---------------------------------------------------------
                $renameTypes = @{ "TOPOLOGY" = "sslo_"; "SSL_SETTINGS" = "ssloT_"; "SECURITY_POLICY" = "ssloP_" }
                
                # Detect the common base name across the renameable stack
                $stems = @{}
                $renameable = @($backup.blocks | Where-Object { $renameTypes.ContainsKey($_.deploymentType) })
                foreach ($rb in $renameable) {
                    $pfx = $renameTypes[$rb.deploymentType]
                    if ($rb.deploymentName.StartsWith($pfx)) {
                        $stem = $rb.deploymentName.Substring($pfx.Length)
                        if (-not $stems.ContainsKey($stem)) { $stems[$stem] = 0 }
                        $stems[$stem]++
                    }
                }
                
                $detectedBase = $null
                if ($stems.Count -eq 1 -and $renameable.Count -ge 2) {
                    $detectedBase = @($stems.Keys)[0]
                }
                
                if ($detectedBase) {
                    Write-Host ""
                    Write-LogInfo "Detected base name: $detectedBase (topology, SSL settings, security policy)"
                    $newBase = Read-Host "  New base name (Enter to keep $detectedBase)"
                    
                    if (-not [string]::IsNullOrWhiteSpace($newBase) -and $newBase -cne $detectedBase) {
                        # Names cascade into TMOS and APM object names downstream - keep them short and clean
                        if ($newBase -notmatch '^[A-Za-z0-9_]{1,20}$') {
                            Write-LogError "Base name must be 1-20 characters: letters, numbers, underscores."
                            Press-EnterToContinue
                            return
                        }
                        
                        # Rename map: the three full prefixed block names of this stack.
                        # State blocks echo gc-generated artifacts (virtuals, log settings,
                        # generated profiles, .app folder paths) that embed the full block
                        # name in values AND keys, so a field walk cannot enumerate them.
                        # Full names are long, unique, mutually non-substring tokens, so a
                        # per-block textual substitution is deterministic. The round-trip
                        # check proves the only change was the mapped rename
                        $renameMap = @{}
                        foreach ($rb in $renameable) {
                            $renameMap[$rb.deploymentName] = "$($renameTypes[$rb.deploymentType])$newBase"
                        }
                        
                        # Pass 1: rename and verify every block before committing any
                        $renamedBlocks = @{}
                        $renameOk = $true
                        for ($bi = 0; $bi -lt $backup.blocks.Count; $bi++) {
                            $origJson = $backup.blocks[$bi].block | ConvertTo-Json -Depth 30
                            $newJson = $origJson
                            foreach ($oldName in $renameMap.Keys) {
                                $newJson = $newJson.Replace($oldName, $renameMap[$oldName])
                            }
                            # Round-trip invariant: reverse substitution must restore the
                            # original exactly - proves no dependency path, chain, or
                            # service reference was altered, and that the new base does
                            # not collide with any name already present in the block
                            $revJson = $newJson
                            foreach ($oldName in $renameMap.Keys) {
                                $revJson = $revJson.Replace($renameMap[$oldName], $oldName)
                            }
                            if ($revJson -cne $origJson) {
                                $renameOk = $false
                                break
                            }
                            $renamedBlocks[$bi] = $newJson
                        }
                        
                        if (-not $renameOk) {
                            Write-LogError "Rename verification failed - the new base name conflicts with existing content."
                            Write-LogError "Replay aborted, no changes made."
                            Press-EnterToContinue
                            return
                        }
                        
                        # Pass 2: commit
                        for ($bi = 0; $bi -lt $backup.blocks.Count; $bi++) {
                            $backup.blocks[$bi].block = $renamedBlocks[$bi] | ConvertFrom-Json
                            if ($renameMap.ContainsKey($backup.blocks[$bi].deploymentName)) {
                                $backup.blocks[$bi].deploymentName = $renameMap[$backup.blocks[$bi].deploymentName]
                            }
                        }
                        
                        Write-LogOk "Base name applied: $detectedBase -> $newBase"
                    }
                }
            } else {
                Write-LogWarn "Invalid selection."
                Press-EnterToContinue
                return
            }
        } elseif ($scopeChoice -ne "1") {
            Write-LogWarn "Invalid selection."
            Press-EnterToContinue
            return
        }
    }
    
    # -------------------------------------------------------------------------
    # Step 5: Prerequisite validation
    # -------------------------------------------------------------------------
    Clear-Host
    Write-LogSection "Prerequisite Validation"
    Write-Host ""
    Write-LogStep "Validating prerequisites on target..."
    Write-Host ""
    
    $script:missingPrereqs = @()
    $script:checkedPrereqs = @{}
    
    # Helper: check a single object exists on target
    function Test-PrereqExists {
        param([string]$Endpoint, [string]$ObjPath, [string]$Label, [string]$RequiredBy)
        
        if ([string]::IsNullOrWhiteSpace($ObjPath) -or $ObjPath -eq "none") { return }
        # Skip SSLO-generated objects - these are created by the gc processor
        if ($ObjPath -match "\.app/" -or $ObjPath -match "/Common/sslo" -or $ObjPath -match "/Common/f5-ssl-orchestrator") { return }
        $checkKey = "${Endpoint}:${ObjPath}"
        if ($script:checkedPrereqs.ContainsKey($checkKey)) { return }
        $script:checkedPrereqs[$checkKey] = $true
        
        $objName = $ObjPath -replace "^/Common/", ""
        $result = Invoke-F5Get -Endpoint "${Endpoint}~Common~${objName}"
        if ($result.Success) {
            Write-Host "    ${Label}: ${ObjPath}" -ForegroundColor White
        } else {
            Write-Host "    ${Label}: ${ObjPath}" -NoNewline -ForegroundColor White
            Write-Host " - MISSING" -ForegroundColor Red
            $script:missingPrereqs += @{ Label = $Label; Path = $ObjPath; RequiredBy = $RequiredBy }
        }
    }
    
    # SSLO General Settings must exist on the target - early gate
    Write-Host "  SSLO General Settings" -ForegroundColor White
    $gsResult = Invoke-F5Get -Endpoint "/mgmt/tm/sys/folder/~Common~ssloGS_global.app"
    if (-not $gsResult.Success) {
        Write-Host ""
        Write-LogError "SSLO General Settings (ssloGS_global) not found on target."
        Write-LogError "Configure General Settings via the SSLO GUI before replay."
        Press-EnterToContinue
        return
    }
    
    foreach ($entry in $backup.blocks) {
        if ($entry.deploymentType -eq "GENERAL_SETTINGS") { continue }
        $depName = $entry.deploymentName
        $block = $entry.block
        $typeLabel = Get-TypeLabel $entry.deploymentType
        
        # Extract the type-specific config data from the state block
        $dataPropertyId = $script:TYPE_PROPERTY_MAP[$entry.deploymentType]
        $configData = $null
        if ($dataPropertyId) {
            foreach ($prop in $block.inputProperties) {
                if ($prop.id -eq $dataPropertyId) {
                    $configData = $prop.value
                    break
                }
            }
        }
        if ($null -eq $configData) { continue }
        
        switch ($entry.deploymentType) {
            
            # SSL_SETTINGS: certs, keys, chains, CA bundle, cipher groups, log publishers
            "SSL_SETTINGS" {
                Write-Host "" 
                Write-Host "  $typeLabel $depName" -ForegroundColor White
                # Client cert/key chains
                foreach ($section in @("certKeyChain", "caCertKeyChain")) {
                    if ($configData.clientSettings -and $configData.clientSettings.$section) {
                        foreach ($chain in $configData.clientSettings.$section) {
                            Test-PrereqExists "/mgmt/tm/sys/crypto/cert/" $chain.cert "Certificate" $depName
                            Test-PrereqExists "/mgmt/tm/sys/crypto/key/" $chain.key "Key" $depName
                            if ($chain.chain -and $chain.chain -ne "") {
                                Test-PrereqExists "/mgmt/tm/sys/crypto/cert/" $chain.chain "Chain cert" $depName
                            }
                        }
                    }
                }
                # Cipher groups
                if ($configData.clientSettings -and $configData.clientSettings.ciphers) {
                    $cg = $configData.clientSettings.ciphers.cipherGroup
                    if ($cg -and $cg -ne "") {
                        Test-PrereqExists "/mgmt/tm/ltm/cipher/group/" $cg "Cipher group" $depName
                    }
                }
                if ($configData.serverSettings -and $configData.serverSettings.ciphers) {
                    $cg = $configData.serverSettings.ciphers.cipherGroup
                    if ($cg -and $cg -ne "") {
                        Test-PrereqExists "/mgmt/tm/ltm/cipher/group/" $cg "Cipher group" $depName
                    }
                }
                # Server CA bundle
                if ($configData.serverSettings -and $configData.serverSettings.caBundle) {
                    Test-PrereqExists "/mgmt/tm/sys/crypto/cert/" $configData.serverSettings.caBundle "CA bundle" $depName
                }
                # Log publishers
                if ($configData.clientSettings -and $configData.clientSettings.logPublisher) {
                    Test-PrereqExists "/mgmt/tm/sys/log-config/publisher/" $configData.clientSettings.logPublisher "Log publisher" $depName
                }
                if ($configData.serverSettings -and $configData.serverSettings.logPublisher) {
                    Test-PrereqExists "/mgmt/tm/sys/log-config/publisher/" $configData.serverSettings.logPublisher "Log publisher" $depName
                }
            }
            
            # SERVICE: VLANs, monitors, SNAT pools, iRules
            # Field paths from bigip_sslo_service_layer3.py, bigip_sslo_service_icap.py,
            # bigip_sslo_service_http.py, bigip_sslo_service_layer2.py, bigip_sslo_service_tap.py
            "SERVICE" {
                Write-Host ""
                Write-Host "  $typeLabel $depName" -ForegroundColor White
                $svcList = if ($configData -is [System.Array]) { $configData } else { @($configData) }
                foreach ($svc in $svcList) {
                    # VLANs - L3, HTTP, ICAP: connectionInformation.{to,from}BigipNetwork.vlan.path
                    if ($svc.customService -and $svc.customService.connectionInformation) {
                        $connInfo = $svc.customService.connectionInformation
                        foreach ($dir in @("fromBigipNetwork", "toBigipNetwork")) {
                            if ($connInfo.$dir -and $connInfo.$dir.vlan -and $connInfo.$dir.vlan.path) {
                                Test-PrereqExists "/mgmt/tm/net/vlan/" $connInfo.$dir.vlan.path "VLAN" $depName
                            }
                        }
                    }
                    # VLANs - L2: serviceSpecific.devices[].{fromBigipVlan,toBigipVlan}.path
                    if ($svc.customService -and $svc.customService.serviceSpecific -and $svc.customService.serviceSpecific.devices) {
                        foreach ($device in $svc.customService.serviceSpecific.devices) {
                            if ($device.fromBigipVlan -and $device.fromBigipVlan.path) {
                                Test-PrereqExists "/mgmt/tm/net/vlan/" $device.fromBigipVlan.path "VLAN" $depName
                            }
                            if ($device.toBigipVlan -and $device.toBigipVlan.path) {
                                Test-PrereqExists "/mgmt/tm/net/vlan/" $device.toBigipVlan.path "VLAN" $depName
                            }
                        }
                    }
                    # VLANs - TAP: serviceSpecific.vlan.path
                    if ($svc.customService -and $svc.customService.serviceSpecific -and $svc.customService.serviceSpecific.vlan) {
                        if ($svc.customService.serviceSpecific.vlan.path) {
                            Test-PrereqExists "/mgmt/tm/net/vlan/" $svc.customService.serviceSpecific.vlan.path "VLAN" $depName
                        }
                    }
                    # SNAT pool: snatConfiguration.snat.referredObj
                    if ($svc.snatConfiguration -and $svc.snatConfiguration.snat -and $svc.snatConfiguration.snat.referredObj) {
                        $snatRef = $svc.snatConfiguration.snat.referredObj
                        if ($snatRef -ne "" -and $snatRef -match "^/Common/") {
                            Test-PrereqExists "/mgmt/tm/ltm/snatpool/" $snatRef "SNAT pool" $depName
                        }
                    }
                    # iRules: customService.iRuleList[].name
                    if ($svc.customService -and $svc.customService.iRuleList) {
                        foreach ($rule in $svc.customService.iRuleList) {
                            $ruleName = if ($rule.name) { $rule.name } elseif ($rule -is [string]) { $rule } else { $null }
                            if ($ruleName -and $ruleName -match "^/Common/" -and $ruleName -notmatch "\.app/") {
                                Test-PrereqExists "/mgmt/tm/ltm/rule/" $ruleName "iRule" $depName
                            }
                        }
                    }
                }
            }
            
            # TOPOLOGY: VLANs, SNAT pools, TCP profiles, L7 profile, iRules, LTM policies, gateway pools
            # Field paths from bigip_sslo_config_topology.py
            "TOPOLOGY" {
                Write-Host ""
                Write-Host "  $typeLabel $depName" -ForegroundColor White
                # Ingress VLANs: ingressNetwork.vlans[].name
                if ($configData.ingressNetwork -and $configData.ingressNetwork.vlans) {
                    foreach ($vlan in $configData.ingressNetwork.vlans) {
                        $vlanPath = if ($vlan.name) { $vlan.name } elseif ($vlan -is [string]) { $vlan } else { $null }
                        if ($vlanPath -and $vlanPath -match "^/Common/" -and $vlanPath -notmatch "\.app/") {
                            Test-PrereqExists "/mgmt/tm/net/vlan/" $vlanPath "VLAN" $depName
                        }
                    }
                }
                # SNAT pool: egressNetwork.snat.referredObj
                if ($configData.egressNetwork -and $configData.egressNetwork.snat -and $configData.egressNetwork.snat.referredObj) {
                    $snatRef = $configData.egressNetwork.snat.referredObj
                    if ($snatRef -ne "" -and $snatRef -match "^/Common/") {
                        Test-PrereqExists "/mgmt/tm/ltm/snatpool/" $snatRef "SNAT pool" $depName
                    }
                }
                # Gateway pool: egressNetwork.outboundGateways.referredObj
                # Field name per Ansible bigip_sslo_config_topology.py - 'gatewayPool' does not exist
                if ($configData.egressNetwork -and $configData.egressNetwork.outboundGateways -and $configData.egressNetwork.outboundGateways.referredObj) {
                    $gwPool = $configData.egressNetwork.outboundGateways.referredObj
                    if ($gwPool -ne "" -and $gwPool -match "^/Common/") {
                        Test-PrereqExists "/mgmt/tm/ltm/pool/" $gwPool "Gateway pool" $depName
                    }
                }
                # TCP profiles: tcpSettings.clientTcpProfile / serverTcpProfile
                if ($configData.tcpSettings -and $configData.tcpSettings.clientTcpProfile -and $configData.tcpSettings.clientTcpProfile -match "^/Common/") {
                    Test-PrereqExists "/mgmt/tm/ltm/profile/tcp/" $configData.tcpSettings.clientTcpProfile "TCP profile" $depName
                }
                if ($configData.tcpSettings -and $configData.tcpSettings.serverTcpProfile -and $configData.tcpSettings.serverTcpProfile -match "^/Common/") {
                    Test-PrereqExists "/mgmt/tm/ltm/profile/tcp/" $configData.tcpSettings.serverTcpProfile "TCP profile" $depName
                }
                # L7 profile: l7Profile
                if ($configData.l7Profile -and $configData.l7Profile -match "^/Common/") {
                    Test-PrereqExists "/mgmt/tm/ltm/profile/http/" $configData.l7Profile "HTTP profile" $depName
                }
                # Custom iRules: iRulesList
                if ($configData.iRulesList) {
                    foreach ($rule in $configData.iRulesList) {
                        $ruleName = if ($rule.name) { $rule.name } elseif ($rule -is [string]) { $rule } else { $null }
                        if ($ruleName -and $ruleName -match "^/Common/" -and -not (Test-IsSsloGenerated -ObjPath $ruleName)) {
                            Test-PrereqExists "/mgmt/tm/ltm/rule/" $ruleName "iRule" $depName
                        }
                    }
                }
                # LTM policies: cpmPolicies
                if ($configData.cpmPolicies) {
                    foreach ($pol in $configData.cpmPolicies) {
                        $polPath = if ($pol -is [string]) { $pol } elseif ($pol.name) { $pol.name } else { $null }
                        if ($polPath -and $polPath -match "^/Common/") {
                            Test-PrereqExists "/mgmt/tm/ltm/policy/" $polPath "LTM policy" $depName
                        }
                    }
                }
            }
            
            # SECURITY_POLICY: datagroups and URL categories referenced in rule conditions
            # Field paths from bigip_sslo_config_policy.py condition handling
            "SECURITY_POLICY" {
                Write-Host ""
                Write-Host "  $typeLabel $depName" -ForegroundColor White
                
                $allRules = @()
                if ($configData.rules) { $allRules += $configData.rules }
                
                $checkedCategories = @{}
                $checkedDatagroups = @{}
                
                foreach ($rule in $allRules) {
                    foreach ($condition in $rule.conditions) {
                        if (-not $condition.options) { continue }
                        $opts = $condition.options
                        
                        # Datagroups in subnet match conditions
                        if ($opts.subnet) {
                            foreach ($entry in $opts.subnet) {
                                if ($entry.valueType -eq "datagroup" -and $entry.subnet -and $entry.subnet -match "^/Common/") {
                                    $dgPath = $entry.subnet
                                    if ($checkedDatagroups.ContainsKey($dgPath)) { continue }
                                    $checkedDatagroups[$dgPath] = $true
                                    
                                    $dgName = $dgPath -replace "^/Common/", ""
                                    $dgResult = Invoke-F5Get -Endpoint "/mgmt/tm/ltm/data-group/internal/~Common~${dgName}"
                                    if ($dgResult.Success) {
                                        # Validate type matches condition
                                        $expectedType = "ip"
                                        $actualType = $dgResult.Response.type
                                        if ($actualType -ne $expectedType) {
                                            Write-Host "    Datagroup: $dgPath" -NoNewline -ForegroundColor White
                                            Write-Host " - WRONG TYPE (is $actualType, policy expects $expectedType)" -ForegroundColor Red
                                            $script:missingPrereqs += @{ Label = "Datagroup (type mismatch)"; Path = $dgPath; RequiredBy = $depName }
                                        } else {
                                            Write-Host "    Datagroup: $dgPath ($actualType)" -ForegroundColor White
                                        }
                                    } else {
                                        Write-Host "    Datagroup: $dgPath" -NoNewline -ForegroundColor White
                                        Write-Host " - MISSING" -ForegroundColor Red
                                        $script:missingPrereqs += @{ Label = "Datagroup"; Path = $dgPath; RequiredBy = $depName }
                                    }
                                }
                            }
                        }
                        
                        # URL categories in category lookup conditions
                        # Built-in categories always exist - only validate custom ones
                        if ($opts.category) {
                            foreach ($catName in $opts.category) {
                                if (-not $catName -or $catName -eq "") { continue }
                                if ($checkedCategories.ContainsKey($catName)) { continue }
                                $checkedCategories[$catName] = $true
                                
                                # Skip built-in categories (from Ansible condition_category_list)
                                if ($script:BUILTIN_URL_CATEGORIES.ContainsKey($catName)) { continue }
                                
                                $catEncoded = $catName -replace " ", "%20"
                                $catResult = Invoke-F5Get -Endpoint "/mgmt/tm/sys/url-db/url-category/~Common~${catEncoded}"
                                if ($catResult.Success) {
                                    Write-Host "    URL category: $catName" -ForegroundColor White
                                } else {
                                    Write-Host "    URL category: $catName" -NoNewline -ForegroundColor White
                                    Write-Host " - MISSING" -ForegroundColor Red
                                    $script:missingPrereqs += @{ Label = "URL category"; Path = $catName; RequiredBy = $depName }
                                }
                            }
                        }
                    }
                }
            }
        }
    }
    
    # -------------------------------------------------------------------------
    # Step 5a: Check for missing prerequisites
    # -------------------------------------------------------------------------
    if ($script:missingPrereqs.Count -gt 0) {
        Write-Host ""
        Write-LogError "$($script:missingPrereqs.Count) missing prerequisite(s). Install them on the target before replaying."
        Write-Host ""
        foreach ($missing in $script:missingPrereqs) {
            Write-Host "    $($missing.Label): $($missing.Path)" -NoNewline -ForegroundColor Red
            Write-Host " (required by $($missing.RequiredBy))" -ForegroundColor DarkGray
        }
        Write-Host ""
        Write-Host "    See the dependency manifest file for configs and reference." -ForegroundColor Yellow
        Write-Host ""
        Press-EnterToContinue
        return
    }
    
    Write-LogOk "All prerequisites verified ($($script:checkedPrereqs.Count) objects checked)"
    
    Start-Sleep -Seconds 2
    
    # -------------------------------------------------------------------------
    # Step 6: Classify blocks and check for existing objects
    # -------------------------------------------------------------------------
    Clear-Host
    Write-LogSection "Pre-Replay Analysis"
    Write-Host ""
    Write-LogStep "Checking for existing SSLO objects on target..."
    
    $restorePlan = @()
    
    # Build target inventory with block IDs and states.
    # A failed GET here MUST abort: an empty inventory disables collision
    # detection and converts "skip what exists" into "deploy everything"
    $targetBlocks = @{}
    $targetResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks"
    if (-not $targetResult.Success) {
        Write-LogError "Failed to retrieve target inventory (HTTP $($targetResult.StatusCode))."
        Write-LogError "Cannot verify existing objects on the target. Replay aborted - no changes made."
        Press-EnterToContinue
        return
    }
    if ($targetResult.Response.items) {
        foreach ($tb in $targetResult.Response.items) {
            # Index by block name for lookup
            $targetBlocks[$tb.name] = @{
                Id    = $tb.id
                State = $tb.state
                Name  = $tb.name
            }
        }
    }
    
    foreach ($entry in $backup.blocks) {
        $status = "pending"
        $reason = ""
        
        # General settings are environment-specific and not replayed by this tool
        if ($entry.deploymentType -eq "GENERAL_SETTINGS") {
            continue
        }
        
        # Check if deployment name exists on target
        $targetMatch = $targetBlocks[$entry.deploymentName]
        
        if ($targetMatch) {
            # Object exists on target - skip regardless of state
            $status = "skip_exists"
            $reason = "already exists on target"
            if ($targetMatch.State -notin @("BOUND", "UNBOUND")) {
                $reason = "exists on target in $($targetMatch.State) state"
            }
        }
        
        $restorePlan += @{
            DeploymentType = $entry.deploymentType
            DeploymentName = $entry.deploymentName
            BackupType     = $entry.backupType
            Block          = $entry.block
            Status         = $status
            Reason         = $reason
            TargetState    = if ($targetMatch) { $targetMatch.State } else { "" }
        }
    }
    
    # Display replay plan
    $pendingCount = ($restorePlan | Where-Object { $_.Status -eq "pending" }).Count
    $skipExistCount = ($restorePlan | Where-Object { $_.Status -eq "skip_exists" }).Count
    $actionCount = $pendingCount
    
    Write-Host ""
    $lastType = ""
    foreach ($item in $restorePlan) {
        if ($item.DeploymentType -ne $lastType) {
            $lastType = $item.DeploymentType
            Write-Host ""
            Write-Host "  $(Get-TypeDisplayName $lastType)" -ForegroundColor White
        }
        switch ($item.Status) {
            "pending" {
                Write-Host "    $($item.DeploymentName)" -ForegroundColor Green
            }
            "skip_exists" {
                Write-Host "    $($item.DeploymentName)" -NoNewline -ForegroundColor DarkGray
                if ($item.TargetState -and $item.TargetState -notin @("BOUND", "UNBOUND")) {
                    Write-Host " (exists - $($item.TargetState) state)" -ForegroundColor Red
                } else {
                    Write-Host " (exists)" -ForegroundColor DarkGray
                }
            }
        }
    }
    
    Write-Host ""
    Write-Host "  $actionCount to replay, $skipExistCount already exist" -ForegroundColor White
    
    $stuckExisting = @($restorePlan | Where-Object { $_.Status -eq "skip_exists" -and $_.TargetState -and $_.TargetState -notin @("BOUND", "UNBOUND") })
    if ($stuckExisting.Count -gt 0) {
        Write-Host ""
        Write-LogWarn "$($stuckExisting.Count) existing object(s) are in a failed or stuck state and will be skipped."
        Write-LogWarn "Remove them on the target before re-replaying those objects."
    }
    
    if ($actionCount -eq 0) {
        Write-LogWarn "Nothing to replay."
        Press-EnterToContinue
        return
    }
    
    # -------------------------------------------------------------------------
    # Step 7: Confirm replay
    # -------------------------------------------------------------------------
    Write-Host ""
    $confirm = Read-Host "  Type REPLAY to proceed (anything else cancels)"
    if ($confirm -cne "REPLAY") {
        Write-LogInfo "Replay cancelled. No changes were made."
        Press-EnterToContinue
        return
    }
    
    # -------------------------------------------------------------------------
    # Step 8: Execute replay
    # -------------------------------------------------------------------------
    Clear-Host
    $progressText = "Progress: 0 objects installed, 0 failed"
    Write-LogSection "Replaying Snapshot - $progressText"
    Write-Host ""
    
    $restored = @()
    $failed = @()
    $skipped = @()
    $unverified = @()
    $consecutiveFailures = 0
    $script:PfIdRegenerated = 0
    $script:PassphraseCache = @{}
    $lastDeploymentType = ""
    
    foreach ($item in $restorePlan) {
        if ($item.Status -ne "pending") {
            $skipped += "$($item.DeploymentName) ($($item.Reason))"
            continue
        }
        
        # Deployment type transition - refresh screen with updated progress
        if ($item.DeploymentType -ne $lastDeploymentType) {
            if ($lastDeploymentType -ne "") {
                Start-Sleep -Seconds 10
            }
            Clear-Host
            $progressText = "Progress: $($restored.Count) objects installed, $($failed.Count) failed"
            Write-LogSection "Replaying Snapshot - $progressText"
            Write-Host ""
            Write-Host "  $(Get-TypeDisplayName $item.DeploymentType)" -ForegroundColor White
            Write-Host ""
            
            $lastDeploymentType = $item.DeploymentType
        }
        
        Write-LogStep "Creating $(Get-TypeLabel $item.DeploymentType) $($item.DeploymentName)..."
        
        # Prepare the block for POST
        $postBlock = $null
        
        # Topology blocks always go through CREATE conversion regardless of backupType.
        # Replayable topology blocks contain embedded dependent objects (policy, service
        # chains, SSL settings) that would cause the gc processor to create duplicate
        # component blocks when standalone blocks for those objects already exist.
        if ($item.BackupType -eq $script:CAT_STATE -or $item.DeploymentType -eq "TOPOLOGY") {
            $postBlock = Convert-StateBlockToCreate `
                -StateBlock $item.Block `
                -DeploymentType $item.DeploymentType `
                -DeploymentName $item.DeploymentName
            
            if (-not $postBlock) {
                Write-LogError "$($item.DeploymentName) failed - could not prepare block"
                $failed += $item.DeploymentName
            $consecutiveFailures++
            if ($consecutiveFailures -ge 3) {
                Write-Host ""
                Write-LogWarn "Systemic failure detected: 3 consecutive objects failed."
                $contChoice = Read-Host "  Continue replaying remaining objects? (yes/no) [no]"
                if ($contChoice -ne "yes") {
                    Write-LogInfo "Replay stopped."
                    break
                }
                $consecutiveFailures = 0
            }
                continue
            }
        } else {
            $postBlock = $item.Block | ConvertTo-Json -Depth 30 | ConvertFrom-Json
            
            if ($postBlock.PSObject.Properties["state"]) {
                $postBlock.state = "BINDING"
            } else {
                $postBlock | Add-Member -NotePropertyName "state" -NotePropertyValue "BINDING"
            }
            
            foreach ($prop in $postBlock.inputProperties) {
                if ($prop.id -eq "f5-ssl-orchestrator-operation-context") {
                    $prop.value.operationType = "CREATE"
                    if ($prop.value.PSObject.Properties["deploymentReference"]) {
                        $prop.value.deploymentReference = ""
                    }
                    break
                }
            }
        }
        
        # POST the block
        $postBody = $postBlock | ConvertTo-Json -Depth 30
        $result = Invoke-F5Post -Endpoint "/mgmt/shared/iapp/blocks" -Body $postBody
        
        if (-not $result.Success) {
            Write-LogError "$($item.DeploymentName) failed (HTTP $($result.StatusCode))"
            $failed += $item.DeploymentName
            $consecutiveFailures++
            if ($consecutiveFailures -ge 3) {
                Write-Host ""
                Write-LogWarn "Systemic failure detected: 3 consecutive objects failed."
                $contChoice = Read-Host "  Continue replaying remaining objects? (yes/no) [no]"
                if ($contChoice -ne "yes") {
                    Write-LogInfo "Replay stopped."
                    break
                }
                $consecutiveFailures = 0
            }
            continue
        }
        
        # Poll for BOUND state
        # Topologies take longer - APM profiles, VPE, access policies
        $blockId = $null
        try { $blockId = $result.Response.id } catch { <# non-critical #> }
        
        if ($blockId) {
            $pollMax = if ($item.DeploymentType -eq "TOPOLOGY") { 180 } else { 120 }
            $pollElapsed = 0
            $finalState = "UNKNOWN"
            
            while ($pollElapsed -lt $pollMax) {
                Start-Sleep -Seconds 5
                $pollElapsed += 5
                
                $pollResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks/$blockId"
                if ($pollResult.Success) {
                    $finalState = $pollResult.Response.state
                    if ($finalState -in @("BOUND", "ERROR")) { break }
                }
                
                Write-Host "`r  Waiting for $($item.DeploymentName)...    " -NoNewline
            }
            Write-Host ""
            
            if ($finalState -eq "BOUND") {
                Write-LogOk "$($item.DeploymentName)"
                $restored += $item.DeploymentName
                $consecutiveFailures = 0
            } elseif ($finalState -eq "ERROR") {
                Write-LogError "$($item.DeploymentName) failed (ERROR state)"
                $errDetail = ""
                try { $errDetail = [string]$pollResult.Response.error } catch { <# non-critical #> }
                if ($errDetail) {
                    if ($errDetail.Length -gt 300) { $errDetail = $errDetail.Substring(0, 300) + "..." }
                    Write-LogError "  $errDetail"
                }
                $failed += $item.DeploymentName
            $consecutiveFailures++
            if ($consecutiveFailures -ge 3) {
                Write-Host ""
                Write-LogWarn "Systemic failure detected: 3 consecutive objects failed."
                $contChoice = Read-Host "  Continue replaying remaining objects? (yes/no) [no]"
                if ($contChoice -ne "yes") {
                    Write-LogInfo "Replay stopped."
                    break
                }
                $consecutiveFailures = 0
            }
            } else {
                Write-LogWarn "$($item.DeploymentName) timed out (state: $finalState)"
                $failed += $item.DeploymentName
            $consecutiveFailures++
            if ($consecutiveFailures -ge 3) {
                Write-Host ""
                Write-LogWarn "Systemic failure detected: 3 consecutive objects failed."
                $contChoice = Read-Host "  Continue replaying remaining objects? (yes/no) [no]"
                if ($contChoice -ne "yes") {
                    Write-LogInfo "Replay stopped."
                    break
                }
                $consecutiveFailures = 0
            }
            }
        } else {
            Write-LogWarn "$($item.DeploymentName) - posted but no block ID returned; state not verified"
            $unverified += $item.DeploymentName
            $consecutiveFailures = 0
        }
        
        Write-Host ""
        
        # Allow mcpd to commit the create before the next object
        Start-Sleep -Seconds 5
    }
    
    # Passphrases are no longer needed once all blocks are posted
    $script:PassphraseCache = @{}
    
    # -------------------------------------------------------------------------
    # Step 9: Save configuration
    # -------------------------------------------------------------------------
    if ($restored.Count -gt 0) {
        Write-Host ""
        Write-LogStep "Saving BIG-IP configuration..."
        if (Save-F5Config) {
            Write-LogOk "Configuration saved"
        } else {
            Write-LogWarn "Could not save configuration. Save manually via BIG-IP GUI or tmsh."
        }
    }
    
    # -------------------------------------------------------------------------
    # Step 10: Replay summary
    # -------------------------------------------------------------------------
    Clear-Host
    Write-LogSection "Replay Summary"
    
    if ($restored.Count -gt 0) {
        Write-Host ""
        Write-Host "  Replayed $($restored.Count) objects:" -ForegroundColor Green
        Write-Host ""
        foreach ($name in $restored) {
            Write-Host "    $name" -ForegroundColor White
        }
    }
    
    if ($skipped.Count -gt 0) {
        Write-Host ""
        Write-Host "  Skipped $($skipped.Count) objects:" -ForegroundColor Yellow
        Write-Host ""
        foreach ($name in $skipped) {
            Write-Host "    $name" -ForegroundColor White
        }
    }
    
    if ($unverified.Count -gt 0) {
        Write-Host ""
        Write-Host "  Unverified $($unverified.Count) objects (posted, deployment state unknown):" -ForegroundColor Yellow
        Write-Host ""
        foreach ($name in $unverified) {
            Write-Host "    $name" -ForegroundColor White
        }
    }
    
    if ($failed.Count -gt 0) {
        Write-Host ""
        Write-Host "  Failed $($failed.Count) objects:" -ForegroundColor Red
        Write-Host ""
        foreach ($name in $failed) {
            Write-Host "    $name" -ForegroundColor White
        }
    }
    
    if ($script:PfIdRegenerated -gt 0) {
        Write-Host ""
        Write-LogInfo "Key passphrase prompts were processed during this replay."
        Write-LogInfo "If a key with a passphrase was skipped or mistyped, re-enter it via the SSLO GUI."
    }
    
    Write-Host ""
    if ($failed.Count -eq 0 -and $unverified.Count -eq 0 -and $restored.Count -gt 0) {
        Write-LogOk "Replay complete."
        Write-Host ""
        Write-Host "  NOTE: The SSLO GUI may show a 'not initialized' warning after" -ForegroundColor Yellow
        Write-Host "  replay. This is expected. Open the SSLO GUI and click the" -ForegroundColor Yellow
        Write-Host "  re-trigger/resume icon to reconcile. No configuration is lost." -ForegroundColor Yellow
    } elseif ($failed.Count -gt 0) {
        Write-LogWarn "Replay completed with errors."
    } elseif ($unverified.Count -gt 0) {
        Write-LogWarn "Replay completed with unverified objects. Confirm their state in the SSLO GUI."
    } else {
        Write-LogInfo "No objects were replayed."
    }
    
    Press-EnterToContinue
}


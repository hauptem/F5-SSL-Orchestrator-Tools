# SSLO-Replay - F5 SSL Orchestrator Snapshot and Restore Tool
# =============================================================================
# Version: vb5.3.15-devel (Beta 5 June 12 2026)
# Author: Eric Haupt
# Released under the MIT License.
# https://github.com/hauptem/F5-SSL-Orchestrator-Tools
#
# Requirements: PowerShell 5.1+, BIG-IP TMOS 17.x+ with SSLO 12.x+
#
# REFERENCE:
#   State-to-CREATE transformation logic, per-type inputProperty templates,
#   passphrase token handling, and prerequisite field paths are derived from
#   the F5 Ansible SSLO collection module source code:
#   https://github.com/F5Networks/f5-ansible-bigip
#   Collection: f5networks.f5_bigip 3.15.0-devel
#
# PURPOSE:
#   Captures F5 SSL Orchestrator iAppsLX configuration as a portable JSON
#   snapshot and replays it to the same or different BIG-IP. Solves the
#   problem that UCS restore and F5's native SSLO snapshots fail across
#   devices because iAppsLX block UUIDs are instance-specific.
#
#   The snapshot captures all SSLO objects from the iAppsLX block database,
#   classifies them by deployment type, strips instance-specific fields,
#   captures full configuration of external dependencies (datagroups, URL
#   categories, monitors, profiles, etc.), and writes a single portable
#   JSON file.
#
#   On replay, the tool validates prerequisites and external dependencies,
#   transforms state blocks into gc processor CREATE format with correct
#   per-type inputProperties, regenerates passphrase tokens, and replays
#   objects in dependency order through the iAppsLX block API.
#
# LIMITATIONS:
#   - Certs and keys are not captured or created. These must be installed
#     on the target before replay. The tool validates their presence.
#   - General Settings (ssloGS_global) are environment-specific and not
#     captured. Configure via the SSLO GUI on the target before replay.
#   - Per-request policy modifications made outside SSLO with strict
#     updates disabled will not survive replay. The gc processor rebuilds
#     the PRP from the security policy state data.
#   - Extension services (blocking page, DoH guard) must be installed
#     separately. Their iRules/iFiles are not part of the iAppsLX state.
#   - After replay, the SSLO GUI may display a "not initialized" warning.
#     Click the re-trigger/resume icon to reconcile. Cosmetic only.
#   - Monitors are not validated at prereq time - the gc processor
#     validates at deploy time. Custom monitors must exist on target.
#
# USAGE:
#   .\sslo-replay.ps1
#
# =============================================================================


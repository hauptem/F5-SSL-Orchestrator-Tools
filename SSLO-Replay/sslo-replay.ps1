# Extract operation context from an operation block
# Returns: @{ Operation; Type; Name }, or $null for state blocks
function Get-DeploymentInfo {
    param([object]$Block)
    
    # Check for operation context (operation blocks)
    foreach ($prop in $Block.inputProperties) {
        if ($prop.id -eq "f5-ssl-orchestrator-operation-context") {
            $opType = $prop.value.operationType
            $depType = $prop.value.deploymentType
            $depName = $prop.value.deploymentName
            if ($opType -and $depType) {
                return @{ Operation = $opType; Type = $depType; Name = $depName }
            }
        }
    }
    return $null
}

# Derive the deployment type from a state block's name prefix
# Returns: type string, or $null if not a known SSLO state block
function Get-StateBlockType {
    param([string]$BlockName)
    
    # Check known prefixes (longest first to avoid partial match)
    foreach ($prefix in ($script:STATE_BLOCK_TYPES.Keys | Sort-Object -Property Length -Descending)) {
        if ($BlockName.StartsWith($prefix)) {
            return $script:STATE_BLOCK_TYPES[$prefix]
        }
    }
    
    # Topology blocks use "sslo_" prefix without a type letter
    # e.g. sslo_HTTPS_tcp443 (but NOT sslo_ob_* which are operation blocks)
    if ($BlockName -match "^sslo_" -and $BlockName -notmatch "^sslo_ob_") {
        return "TOPOLOGY"
    }
    
    return $null
}

# Test whether a block is SSLO framework infrastructure (not configuration)
function Test-IsFrameworkBlock {
    param([object]$Block)
    
    # Framework blocks are SSLO infrastructure, not configuration
    if ($Block.name -match "^f5-ssl-orchestrator") { return $true }
    return $false
}

# Test whether a block is SSLO-related by name
function Test-IsSsloBlock {
    param([object]$Block)
    
    if (Test-IsFrameworkBlock -Block $Block) { return $false }
    if ($Block.name -match "^sslo") { return $true }
    
    return $false
}


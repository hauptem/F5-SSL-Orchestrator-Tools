# Force a fresh deployment pass on an existing topology without changing its configuration.
# Reads all SSLO blocks from the device into memory, identifies topologies and shared objects,
# and pushes the selected topology back through the gc processor as a MODIFY operation.
function Invoke-TopologyRedeploy {
    Clear-Host
    Write-LogSection "Redeploy SSLO Topology"
    Write-Host ""
    
    # -------------------------------------------------------------------------
    # Read device configuration into memory
    # -------------------------------------------------------------------------
    Write-LogStep "Reading device configuration..."
    
    $result = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks"
    if (-not $result.Success) {
        Write-LogError "Failed to retrieve iAppsLX blocks."
        Press-EnterToContinue
        return
    }
    
    $allBlocks = $result.Response.items
    if (-not $allBlocks -or $allBlocks.Count -eq 0) {
        Write-LogWarn "No iAppsLX blocks found on this device."
        Press-EnterToContinue
        return
    }
    
    # Classify all SSLO state blocks
    $topologies = @()
    $allObjects = @{}
    
    foreach ($block in $allBlocks) {
        if ($block.state -ne "UNBOUND") { continue }
        $stateType = Get-StateBlockType -BlockName $block.name
        if (-not $stateType) { continue }
        
        $obj = @{
            Name   = $block.name
            Type   = $stateType
            BlockId = $block.id
            Block  = $block
        }
        $allObjects[$block.name] = $obj
        
        if ($stateType -eq "TOPOLOGY") {
            # Extract current policy and SSL references for display
            $currentPolicy = ""
            $currentSsl = @()
            foreach ($prop in $block.inputProperties) {
                if ($prop.id -eq "f5-ssl-orchestrator-topology") {
                    if ($prop.value.securityPolicyReference) {
                        $currentPolicy = $prop.value.securityPolicyReference
                    }
                    if ($prop.value.sslSettingReference) {
                        $refs = $prop.value.sslSettingReference
                        $currentSsl = if ($refs -is [System.Array]) { $refs } else { @($refs) }
                    }
                    break
                }
            }
            $topologies += @{
                Name          = $block.name
                BlockId       = $block.id
                Block         = $block
                CurrentPolicy = $currentPolicy
                CurrentSsl    = $currentSsl
            }
        }
    }
    
    $topoCount = $topologies.Count
    $otherCount = $allObjects.Count - $topoCount
    Write-LogOk "$($allObjects.Count) SSLO objects ($topoCount topologies, $otherCount other)"
    
    if ($topologies.Count -eq 0) {
        Write-LogWarn "No topologies found on this device."
        Press-EnterToContinue
        return
    }
    
    # -------------------------------------------------------------------------
    # Select topology
    # -------------------------------------------------------------------------
    Write-Host ""
    Write-Host "  Topologies:" -ForegroundColor White
    Write-Host ""
    
    for ($i = 0; $i -lt $topologies.Count; $i++) {
        $num = $i + 1
        $topo = $topologies[$i]
        Write-Host "     " -NoNewline
        Write-Host "$num" -NoNewline -ForegroundColor Yellow; Write-Host ")" -NoNewline -ForegroundColor White
        Write-Host "  $($topo.Name)" -NoNewline -ForegroundColor White
        if ($topo.CurrentPolicy) {
            Write-Host "      policy: $($topo.CurrentPolicy)" -ForegroundColor DarkGray
        } else {
            Write-Host ""
        }
    }
    
    Write-Host ""
    $rangeLabel = if ($topologies.Count -eq 1) { "[1]" } else { "[1-$($topologies.Count)]" }
    $topoChoice = Read-Host "  Select $rangeLabel (0 to cancel)"
    
    if ($topoChoice -eq "0") { return }
    
    $selectedTopos = @()
    $topoIdx = 0
    if ([int]::TryParse($topoChoice, [ref]$topoIdx) -and $topoIdx -ge 1 -and $topoIdx -le $topologies.Count) {
        $selectedTopos = @($topologies[$topoIdx - 1])
    } else {
        Write-LogWarn "Invalid selection."
        Press-EnterToContinue
        return
    }
    
    # -------------------------------------------------------------------------
    # Show plan
    # -------------------------------------------------------------------------
    Write-Host ""
    Write-Host "  Redeploy plan:" -ForegroundColor White
    foreach ($topo in $selectedTopos) {
        Write-Host "    [WARN]" -NoNewline -ForegroundColor Yellow
        Write-Host "  MODIFY topology $($topo.Name)" -ForegroundColor White
        # Show topology-specific objects for context
        foreach ($sslName in $topo.CurrentSsl) {
            Write-Host "           SSL settings $sslName" -ForegroundColor DarkGray
        }
        if ($topo.CurrentPolicy) {
            Write-Host "           security policy $($topo.CurrentPolicy)" -ForegroundColor DarkGray
        }
    }
    
    # Untouched: everything not in the selected topology's stack
    $untouched = @()
    foreach ($name in $allObjects.Keys) {
        if ($name -eq $selectedTopos[0].Name) { continue }
        if ($name -in $selectedTopos[0].CurrentSsl) { continue }
        if ($name -eq $selectedTopos[0].CurrentPolicy) { continue }
        $obj = $allObjects[$name]
        $untouched += "$(Get-TypeLabel $obj.Type) $name"
    }
    
    if ($untouched.Count -gt 0) {
        Write-Host ""
        Write-Host "  Untouched:" -ForegroundColor White
        foreach ($item in $untouched) {
            Write-Host "    $item" -ForegroundColor DarkGray
        }
    }
    
    Write-Host ""
    Write-Host "  Forces a fresh deployment pass through the gc processor." -ForegroundColor DarkGray
    Write-Host "  No configuration changes are made." -ForegroundColor DarkGray
    $confirm = Read-Host "  Proceed? [y/N]"
    if ($confirm -notin @("y", "Y", "yes", "YES")) { return }
    
    # -------------------------------------------------------------------------
    # Clean up stuck blocks before redeploy
    # Stuck blocks (BINDING/UNBINDING) cannot be transitioned to UNBOUND via
    # the remote API; patching to ERROR and then deleting is the sequence the
    # framework accepts
    # -------------------------------------------------------------------------
    Clear-Host
    Write-LogSection "Redeploying"
    Write-Host ""
    
    Write-LogStep "Checking for stuck blocks..."
    $stuckCleaned = 0
    
    $allBlocksResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks"
    if ($allBlocksResult.Success -and $allBlocksResult.Response.items) {
        foreach ($topo in $selectedTopos) {
            foreach ($block in $allBlocksResult.Response.items) {
                # Skip healthy states (BOUND, UNBOUND, TEMPLATE)
                if ($block.state -in @("BOUND", "UNBOUND", "TEMPLATE")) { continue }
                # Match by name - operation blocks END with the deployment name
                # (sslo_ob_TOPOLOGY_<OP>_<name>). Anchored match prevents clearing
                # blocks of a sibling topology whose name contains this one
                if ($block.name -ne $topo.Name -and $block.name -notmatch ("_" + [regex]::Escape($topo.Name) + "$")) { continue }
                
                Write-LogStep "Clearing stuck block (state: $($block.state), block: $($block.name))..."
                
                # Patch to ERROR first (the one transition the framework accepts remotely), then DELETE
                Invoke-F5Patch -Endpoint "/mgmt/shared/iapp/blocks/$($block.id)" -Body @{ state = "ERROR" } | Out-Null
                Start-Sleep -Seconds 2
                Invoke-F5Delete -Endpoint "/mgmt/shared/iapp/blocks/$($block.id)" | Out-Null
                $stuckCleaned++
            }
        }
    }
    
    if ($stuckCleaned -gt 0) {
        Write-LogOk "$stuckCleaned stuck block(s) cleared"
        Write-Host ""
        Start-Sleep -Seconds 5
    } else {
        Write-LogOk "No stuck blocks found"
        Write-Host ""
    }
    
    # -------------------------------------------------------------------------
    # Execute redeploy
    # -------------------------------------------------------------------------
    
    $redeployFailed = $false
    
    foreach ($topo in $selectedTopos) {
        Write-LogStep "Redeploying topology $($topo.Name)..."
        
        $modifyBlock = Convert-StateBlockToModify `
            -TargetStateBlock $topo.Block `
            -TopologyName $topo.Name `
            -BlockId $topo.BlockId `
            -NewPolicyName $topo.CurrentPolicy
        
        if (-not $modifyBlock) {
            Write-LogError "Could not prepare MODIFY block for $($topo.Name)."
            $redeployFailed = $true
            break
        }
        
        if (-not (Invoke-BlockPost -Block $modifyBlock -Label "topology $($topo.Name)" -PollMax 180)) {
            Write-LogError "Redeploy failed for $($topo.Name)."
            $redeployFailed = $true
            break
        }
        
        Write-Host ""
    }
    
    if ($redeployFailed) {
        Press-EnterToContinue
        return
    }
    
    # -------------------------------------------------------------------------
    # Save configuration
    # -------------------------------------------------------------------------
    Write-LogStep "Saving BIG-IP configuration..."
    if (Save-F5Config) {
        Write-LogOk "Configuration saved"
    } else {
        Write-LogWarn "Could not save configuration. Save manually via BIG-IP GUI or tmsh."
    }
    
    Write-Host ""
    if ($selectedTopos.Count -eq 1) {
        Write-LogOk "$($selectedTopos[0].Name) redeployed"
    } else {
        Write-LogOk "$($selectedTopos.Count) topologies redeployed"
    }
    
    Press-EnterToContinue
}


# Strip runtime and audit fields from a block before snapshot
function Remove-RuntimeFields {
    param([object]$Block)
    
    # Deep clone via JSON round-trip to get a mutable copy
    $clean = $Block | ConvertTo-Json -Depth 30 | ConvertFrom-Json
    
    # Strip top-level runtime fields
    foreach ($field in $script:RUNTIME_FIELDS) {
        if ($clean.PSObject.Properties[$field]) {
            $clean.PSObject.Properties.Remove($field)
        }
    }
    
    # Strip runtime fields from inputProperties values
    if ($clean.inputProperties) {
        foreach ($prop in $clean.inputProperties) {
            # Handle single object values
            if ($prop.value -is [PSCustomObject]) {
                foreach ($field in $script:RUNTIME_INPUT_FIELDS) {
                    if ($prop.value.PSObject.Properties[$field]) {
                        $prop.value.PSObject.Properties.Remove($field)
                    }
                }
            }
            # Handle array values (services, service chains, etc.)
            if ($prop.value -is [System.Array]) {
                foreach ($item in $prop.value) {
                    if ($item -is [PSCustomObject]) {
                        foreach ($field in $script:RUNTIME_INPUT_FIELDS) {
                            if ($item.PSObject.Properties[$field]) {
                                $item.PSObject.Properties.Remove($field)
                            }
                        }
                    }
                }
            }
        }
    }
    
    # Strip dataProperties (runtime state, not configuration)
    if ($clean.PSObject.Properties["dataProperties"]) {
        $clean.PSObject.Properties.Remove("dataProperties")
    }
    
    # Strip audit section
    if ($clean.PSObject.Properties["audit"]) {
        $clean.PSObject.Properties.Remove("audit")
    }
    
    return $clean
}


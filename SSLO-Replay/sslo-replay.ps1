# Fields to strip from state block config data before CREATE
# These are generated by the gc processor and are NOT valid input
$script:STATE_STRIP_FIELDS = @(
    "profiles"
    "existingBlockId"
    "restrictedId"
    "restrictedHash"
)

# Strip gc-generated fields from config data before a CREATE
function Remove-GeneratedFields {
    param([object]$ConfigData)
    
    # Deep clone
    $clean = $ConfigData | ConvertTo-Json -Depth 30 | ConvertFrom-Json
    
    # Strip generated fields from top level
    foreach ($field in $script:STATE_STRIP_FIELDS) {
        if ($clean -is [PSCustomObject] -and $clean.PSObject.Properties[$field]) {
            $clean.PSObject.Properties.Remove($field)
        }
    }
    
    # If array (services), strip from each element
    if ($clean -is [System.Array]) {
        $cleanArray = @()
        foreach ($item in $clean) {
            if ($item -is [PSCustomObject]) {
                foreach ($field in $script:STATE_STRIP_FIELDS) {
                    if ($item.PSObject.Properties[$field]) {
                        $item.PSObject.Properties.Remove($field)
                    }
                }
            }
            $cleanArray += $item
        }
        return $cleanArray
    }
    
    return $clean
}

# Build a CREATE operation block from a snapshot state block
# Returns: block object ready to POST, or $null on failure
function Convert-StateBlockToCreate {
    param(
        [object]$StateBlock,
        [string]$DeploymentType,
        [string]$DeploymentName
    )
    
    $dataPropertyId = $script:TYPE_PROPERTY_MAP[$DeploymentType]
    if (-not $dataPropertyId) {
        Write-LogError "No property mapping for deployment type: $DeploymentType"
        return $null
    }
    
    $typeProperties = $script:TYPE_INPUT_PROPERTIES[$DeploymentType]
    if (-not $typeProperties) {
        Write-LogError "No input property template for deployment type: $DeploymentType"
        return $null
    }
    
    # Extract the config data from the state block
    $configData = $null
    foreach ($prop in $StateBlock.inputProperties) {
        if ($prop.id -eq $dataPropertyId) {
            $configData = $prop.value
            break
        }
    }
    
    if ($null -eq $configData) {
        Write-LogError "No config data found in property '$dataPropertyId' for $DeploymentName"
        return $null
    }
    
    # Strip generated fields from the config data
    $configData = Remove-GeneratedFields -ConfigData $configData
    
    # Build inputProperties: operation context + type-specific properties
    $inputProps = @()
    
    # Operation context (always first)
    $inputProps += @{
        id = "f5-ssl-orchestrator-operation-context"
        type = "JSON"
        value = [ordered]@{
            operationType = "CREATE"
            deploymentType = $DeploymentType
            deploymentName = $DeploymentName
            deploymentReference = ""
            partition = "Common"
            strictness = $false
        }
    }
    
    # Type-specific properties - slot config data into the matching one
    foreach ($propDef in $typeProperties) {
        if ($propDef.id -eq $dataPropertyId) {
            # This is the property that gets the config data
            $inputProps += @{
                id = $propDef.id
                type = "JSON"
                value = $configData
            }
        } elseif ($DeploymentType -eq "TOPOLOGY") {
            # Topology CREATE requires dependent object configs from the target
            # These were already replayed in earlier steps
            $depValue = $null
            
            switch ($propDef.id) {
                "f5-ssl-orchestrator-general-settings" {
                    # Fetch from ssloGS_global state block on target
                    $gsBlockResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks?`$filter=name+eq+'ssloGS_global'"
                    if ($gsBlockResult.Success -and $gsBlockResult.Response.items.Count -gt 0) {
                        $depValue = $gsBlockResult.Response.items[0].inputProperties[0].value
                    }
                }
                "f5-ssl-orchestrator-policy" {
                    # Fetch security policy config from target
                    $policyName = $configData.securityPolicyReference
                    if ($policyName) {
                        $policyResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks?`$filter=name+eq+'$policyName'"
                        if ($policyResult.Success -and $policyResult.Response.items.Count -gt 0) {
                            foreach ($pp in $policyResult.Response.items[0].inputProperties) {
                                if ($pp.id -eq "f5-ssl-orchestrator-policy") {
                                    $depValue = $pp.value
                                    break
                                }
                            }
                        }
                    }
                }
                "f5-ssl-orchestrator-tls" {
                    # Fetch SSL settings config from target
                    $sslRefs = $configData.sslSettingReference
                    if ($sslRefs -and $sslRefs.Count -gt 0) {
                        $sslName = if ($sslRefs -is [System.Array]) { $sslRefs[0] } else { $sslRefs }
                        $sslResult = Invoke-F5Get -Endpoint "/mgmt/shared/iapp/blocks?`$filter=name+eq+'$sslName'"
                        if ($sslResult.Success -and $sslResult.Response.items.Count -gt 0) {
                            foreach ($sp in $sslResult.Response.items[0].inputProperties) {
                                if ($sp.id -eq "f5-ssl-orchestrator-tls") {
                                    $depValue = $sp.value
                                    break
                                }
                            }
                        }
                    }
                }
            }
            
            $inputProps += @{
                id = $propDef.id
                type = "JSON"
                value = if ($null -ne $depValue) { $depValue } else { $propDef.empty }
            }
        } else {
            # Empty placeholder
            $prop = @{
                id = $propDef.id
                type = "JSON"
            }
            if ($null -ne $propDef.empty) {
                $prop.value = $propDef.empty
            }
            $inputProps += $prop
        }
    }
    
    # Build the CREATE block
    $createBlock = [ordered]@{
        name = "sslo_ob_$($DeploymentType)_CREATE_$DeploymentName"
        inputProperties = $inputProps
        configurationProcessorReference = @{
            link = "https://localhost/mgmt/shared/iapp/processors/f5-iappslx-ssl-orchestrator-gc"
        }
        configProcessorTimeoutSeconds = $script:GC_PROCESSOR_TIMEOUT
        statsProcessorTimeoutSeconds = 60
        configProcessorAffinity = @{
            processorPolicy = "LOCAL"
            affinityProcessorReference = @{
                link = "https://localhost/mgmt/shared/iapp/affinity/local"
            }
        }
        state = "BINDING"
        presentationHtmlReference = @{
            link = "https://localhost/iapps/f5-iappslx-ssl-orchestrator/sgc/sgcIndex.html"
        }
        operation = "CREATE"
    }
    
    # SSL_SETTINGS: regenerate passphrase tokens
    # The state block has pfId tokens that reference restrictedProperties from the
    # original block. On a new CREATE those tokens don't exist. Generate new ones
    # and add restrictedProperties with empty passphrase values.
    if ($DeploymentType -eq "SSL_SETTINGS") {
        $restrictedProps = @()
        $configJson = ($inputProps | Where-Object { $_.id -eq $dataPropertyId }).value
        
        if ($configJson -is [PSCustomObject]) {
            # Scan certKeyChain and caCertKeyChain for pfId fields
            $chainTypes = @("certKeyChain", "caCertKeyChain")
            foreach ($section in @("clientSettings")) {
                if ($configJson.PSObject.Properties[$section]) {
                    foreach ($chainType in $chainTypes) {
                        $chains = $configJson.$section.$chainType
                        if ($chains -is [System.Array]) {
                            foreach ($entry in $chains) {
                                if ($entry.PSObject.Properties["pfId"]) {
                                    # The GUI assigns a pfId to every cert/key chain whether or not
                                    # the key has a passphrase, so pfId presence proves nothing and
                                    # the passphrase itself is not recoverable from a state block.
                                    # Ask the operator neutrally, matching the Ansible key_passphrase
                                    # contract - Enter means the key has none.
                                    # Cached per key path - the same key referenced by multiple
                                    # chains or SSL settings prompts once per replay run
                                    $certLabel = if ($entry.cert) { $entry.cert } else { $DeploymentName }
                                    $cacheKey = if ($entry.key) { [string]$entry.key } else { $certLabel }
                                    if ($script:PassphraseCache.ContainsKey($cacheKey)) {
                                        $passValue = $script:PassphraseCache[$cacheKey]
                                    } else {
                                        Write-Host ""
                                        Write-LogInfo "$DeploymentName references key $cacheKey"
                                        $securePass = Read-Host "  Key passphrase if this key has one (Enter for none)" -AsSecureString
                                        $BSTR = [System.Runtime.InteropServices.Marshal]::SecureStringToBSTR($securePass)
                                        $passValue = [System.Runtime.InteropServices.Marshal]::PtrToStringAuto($BSTR)
                                        [System.Runtime.InteropServices.Marshal]::ZeroFreeBSTR($BSTR)
                                        $script:PassphraseCache[$cacheKey] = $passValue
                                    }
                                    
                                    $newPfId = "T_" + [string]([DateTimeOffset]::UtcNow.ToUnixTimeMilliseconds()) + [string](Get-Random -Maximum 1000)
                                    $entry.passphrase = $newPfId
                                    $entry.pfId = $newPfId
                                    $restrictedProps += @{
                                        id = $newPfId
                                        type = "STRING"
                                        value = if ($passValue) { $passValue } else { "" }
                                    }
                                    $script:PfIdRegenerated++
                                    # Small delay to ensure unique timestamps
                                    Start-Sleep -Milliseconds 10
                                }
                            }
                        }
                    }
                }
            }
        }
        
        if ($restrictedProps.Count -gt 0) {
            $createBlock["restrictedProperties"] = $restrictedProps
        }
    }
    
    return $createBlock
}


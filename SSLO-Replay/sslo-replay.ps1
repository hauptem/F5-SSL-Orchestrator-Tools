# Paths matching these patterns are created by the gc processor, not prereqs
function Test-IsSsloGenerated {
    param([string]$ObjPath)
    if ([string]::IsNullOrWhiteSpace($ObjPath)) { return $true }
    if ($ObjPath -eq "none" -or $ObjPath -eq "") { return $true }
    if ($ObjPath -match "\.app/") { return $true }
    if ($ObjPath -match "/Common/sslo") { return $true }
    if ($ObjPath -match "/Common/f5-ssl-orchestrator") { return $true }
    return $false
}

# Cert/key endpoints are handled separately in SSL_SETTINGS to capture names
# without going through Add-Ref (which would try to fetch content)
function Test-IsCertKeyEndpoint {
    param([string]$Endpoint)
    if ($Endpoint -match "/sys/crypto/cert/") { return $true }
    if ($Endpoint -match "/sys/crypto/key/") { return $true }
    return $false
}

# Monitors require type probing - the SSLO state data stores only the path,
# not the monitor type, and the REST API requires the type in the endpoint
function Get-MonitorDependency {
    param([string]$MonitorPath)
    
    # Skip built-in monitors
    if ($MonitorPath -in $script:BUILTIN_MONITORS) { return $null }
    
    $objName = $MonitorPath -replace "^/Common/", ""
    
    # Probe each monitor type endpoint until we get a hit
    foreach ($probe in $script:MONITOR_PROBE_ORDER) {
        $fullEndpoint = "$($probe.Endpoint)~Common~${objName}"
        $result = Invoke-F5Get -Endpoint $fullEndpoint
        if ($result.Success) {
            return [ordered]@{
                type       = $probe.Type
                path       = $MonitorPath
                endpoint   = $fullEndpoint
                portable   = $true
                config     = $result.Response
            }
        }
    }
    
    # Unknown monitor type - capture as reference with no config
    return [ordered]@{
        type       = "monitor_unknown"
        path       = $MonitorPath
        endpoint   = ""
        portable   = $false
        config     = $null
    }
}

# Extract all external dependency references from a single block's config data
# Returns: array of @{ Type; Path; Endpoint } records for capture
function Get-BlockDependencyRefs {
    param(
        [string]$DeploymentType,
        [string]$DeploymentName,
        [object]$ConfigData
    )
    
    $script:_depRefs = [System.Collections.ArrayList]::new()
    
    # Helper to add a ref if it's a real external path
    function Add-Ref {
        param([string]$Type, [string]$Path, [string]$Endpoint)
        if ([string]::IsNullOrWhiteSpace($Path)) { return }
        if ($Path -eq "none" -or $Path -eq "") { return }
        if (Test-IsSsloGenerated -ObjPath $Path) { return }
        if ($Path -notmatch "^/Common/") { return }
        if (Test-IsCertKeyEndpoint -Endpoint $Endpoint) { return }
        [void]$script:_depRefs.Add([ordered]@{ Type = $Type; Path = $Path; Endpoint = $Endpoint })
    }
    
    switch ($DeploymentType) {
        
        "SSL_SETTINGS" {
            # Certificates and keys - name only, no content, no secrets
            # These bypass Add-Ref (which filters cert/key endpoints) because we want
            # name-only references in the manifest so the snapshot is self-documenting.
            $certSeen = @{}
            foreach ($section in @("certKeyChain", "caCertKeyChain")) {
                if ($ConfigData.clientSettings -and $ConfigData.clientSettings.$section) {
                    foreach ($chain in $ConfigData.clientSettings.$section) {
                        # Certificate
                        if ($chain.cert -and $chain.cert -ne "" -and $chain.cert -match "^/Common/" -and -not $certSeen[$chain.cert]) {
                            $certSeen[$chain.cert] = $true
                            [void]$script:_depRefs.Add([ordered]@{ Type = "certificate"; Path = $chain.cert; Endpoint = "/mgmt/tm/sys/crypto/cert/" })
                        }
                        # Key (may be same name as cert - tracked separately)
                        $keyPath = "key:$($chain.key)"
                        if ($chain.key -and $chain.key -ne "" -and $chain.key -match "^/Common/" -and -not $certSeen[$keyPath]) {
                            $certSeen[$keyPath] = $true
                            [void]$script:_depRefs.Add([ordered]@{ Type = "key"; Path = $chain.key; Endpoint = "/mgmt/tm/sys/crypto/key/" })
                        }
                        # Chain cert (if different from cert)
                        if ($chain.chain -and $chain.chain -ne "" -and $chain.chain -match "^/Common/" -and -not $certSeen[$chain.chain]) {
                            $certSeen[$chain.chain] = $true
                            [void]$script:_depRefs.Add([ordered]@{ Type = "certificate"; Path = $chain.chain; Endpoint = "/mgmt/tm/sys/crypto/cert/" })
                        }
                    }
                }
            }
            # Server CA bundle
            if ($ConfigData.serverSettings -and $ConfigData.serverSettings.caBundle) {
                $caBundle = $ConfigData.serverSettings.caBundle
                if ($caBundle -and $caBundle -ne "" -and $caBundle -match "^/Common/") {
                    [void]$script:_depRefs.Add([ordered]@{ Type = "ca_bundle"; Path = $caBundle; Endpoint = "/mgmt/tm/sys/crypto/cert/" })
                }
            }
            # Cipher groups - client and server
            if ($ConfigData.clientSettings -and $ConfigData.clientSettings.ciphers) {
                $cg = $ConfigData.clientSettings.ciphers.cipherGroup
                if ($cg -and $cg -ne "") {
                    Add-Ref "cipher_group" $cg "/mgmt/tm/ltm/cipher/group/"
                }
            }
            if ($ConfigData.serverSettings -and $ConfigData.serverSettings.ciphers) {
                $cg = $ConfigData.serverSettings.ciphers.cipherGroup
                if ($cg -and $cg -ne "") {
                    Add-Ref "cipher_group" $cg "/mgmt/tm/ltm/cipher/group/"
                }
            }
            # Log publishers
            if ($ConfigData.clientSettings -and $ConfigData.clientSettings.logPublisher) {
                Add-Ref "log_publisher" $ConfigData.clientSettings.logPublisher "/mgmt/tm/sys/log-config/publisher/"
            }
            if ($ConfigData.serverSettings -and $ConfigData.serverSettings.logPublisher) {
                Add-Ref "log_publisher" $ConfigData.serverSettings.logPublisher "/mgmt/tm/sys/log-config/publisher/"
            }
        }
        
        "SERVICE" {
            # Services can be a single object or array
            $svcList = if ($ConfigData -is [System.Array]) { $ConfigData } else { @($ConfigData) }
            foreach ($svc in $svcList) {
                # VLANs - L3, HTTP, ICAP: connectionInformation.{from,to}BigipNetwork.vlan.path
                if ($svc.customService -and $svc.customService.connectionInformation) {
                    $connInfo = $svc.customService.connectionInformation
                    foreach ($dir in @("fromBigipNetwork", "toBigipNetwork")) {
                        if ($connInfo.$dir -and $connInfo.$dir.vlan -and $connInfo.$dir.vlan.path) {
                            Add-Ref "vlan" $connInfo.$dir.vlan.path "/mgmt/tm/net/vlan/"
                        }
                    }
                }
                # VLANs - L2: serviceSpecific.devices[].{fromBigipVlan,toBigipVlan}.path
                if ($svc.customService -and $svc.customService.serviceSpecific -and $svc.customService.serviceSpecific.devices) {
                    foreach ($device in $svc.customService.serviceSpecific.devices) {
                        if ($device.fromBigipVlan -and $device.fromBigipVlan.path) {
                            Add-Ref "vlan" $device.fromBigipVlan.path "/mgmt/tm/net/vlan/"
                        }
                        if ($device.toBigipVlan -and $device.toBigipVlan.path) {
                            Add-Ref "vlan" $device.toBigipVlan.path "/mgmt/tm/net/vlan/"
                        }
                    }
                }
                # VLANs - TAP: serviceSpecific.vlan.path
                if ($svc.customService -and $svc.customService.serviceSpecific -and $svc.customService.serviceSpecific.vlan) {
                    if ($svc.customService.serviceSpecific.vlan.path) {
                        Add-Ref "vlan" $svc.customService.serviceSpecific.vlan.path "/mgmt/tm/net/vlan/"
                    }
                }
                # Monitor: loadBalancing.monitor.fromSystem
                if ($svc.customService -and $svc.customService.loadBalancing -and $svc.customService.loadBalancing.monitor) {
                    $monPath = $svc.customService.loadBalancing.monitor.fromSystem
                    if ($monPath -and $monPath -ne "" -and $monPath -notin $script:BUILTIN_MONITORS) {
                        # Monitor uses special probe detection - flag with type "monitor"
                        [void]$script:_depRefs.Add([ordered]@{ Type = "monitor"; Path = $monPath; Endpoint = "" })
                    }
                }
                # iRules: customService.iRuleList[].name
                if ($svc.customService -and $svc.customService.iRuleList) {
                    foreach ($rule in $svc.customService.iRuleList) {
                        $ruleName = if ($rule.name) { $rule.name } elseif ($rule -is [string]) { $rule } else { $null }
                        if ($ruleName -and $ruleName -match "^/Common/" -and -not (Test-IsSsloGenerated -ObjPath $ruleName)) {
                            Add-Ref "irule" $ruleName "/mgmt/tm/ltm/rule/"
                        }
                    }
                }
                # SNAT pool: snatConfiguration.snat.referredObj
                if ($svc.snatConfiguration -and $svc.snatConfiguration.snat -and $svc.snatConfiguration.snat.referredObj) {
                    $snatRef = $svc.snatConfiguration.snat.referredObj
                    if ($snatRef -ne "" -and $snatRef -match "^/Common/") {
                        Add-Ref "snatpool" $snatRef "/mgmt/tm/ltm/snatpool/"
                    }
                }
            }
        }
        
        "TOPOLOGY" {
            # Ingress VLANs: ingressNetwork.vlans[].name
            if ($ConfigData.ingressNetwork -and $ConfigData.ingressNetwork.vlans) {
                foreach ($vlan in $ConfigData.ingressNetwork.vlans) {
                    $vlanPath = if ($vlan.name) { $vlan.name } elseif ($vlan -is [string]) { $vlan } else { $null }
                    if ($vlanPath) {
                        Add-Ref "vlan" $vlanPath "/mgmt/tm/net/vlan/"
                    }
                }
            }
            # SNAT pool: egressNetwork.snat.referredObj
            if ($ConfigData.egressNetwork -and $ConfigData.egressNetwork.snat -and $ConfigData.egressNetwork.snat.referredObj) {
                $snatRef = $ConfigData.egressNetwork.snat.referredObj
                if ($snatRef -ne "" -and $snatRef -match "^/Common/") {
                    Add-Ref "snatpool" $snatRef "/mgmt/tm/ltm/snatpool/"
                }
            }
            # Gateway pool: egressNetwork.outboundGateways.referredObj
            # Field name per Ansible bigip_sslo_config_topology.py - 'gatewayPool' does not exist
            if ($ConfigData.egressNetwork -and $ConfigData.egressNetwork.outboundGateways -and $ConfigData.egressNetwork.outboundGateways.referredObj) {
                $gwPool = $ConfigData.egressNetwork.outboundGateways.referredObj
                if ($gwPool -ne "" -and $gwPool -match "^/Common/") {
                    Add-Ref "gateway_pool" $gwPool "/mgmt/tm/ltm/pool/"
                }
            }
            # TCP profiles
            if ($ConfigData.tcpSettings) {
                if ($ConfigData.tcpSettings.clientTcpProfile -and $ConfigData.tcpSettings.clientTcpProfile -match "^/Common/") {
                    Add-Ref "profile_tcp" $ConfigData.tcpSettings.clientTcpProfile "/mgmt/tm/ltm/profile/tcp/"
                }
                if ($ConfigData.tcpSettings.serverTcpProfile -and $ConfigData.tcpSettings.serverTcpProfile -match "^/Common/") {
                    Add-Ref "profile_tcp" $ConfigData.tcpSettings.serverTcpProfile "/mgmt/tm/ltm/profile/tcp/"
                }
            }
            # HTTP profile: l7Profile
            if ($ConfigData.l7Profile -and $ConfigData.l7Profile -match "^/Common/") {
                Add-Ref "profile_http" $ConfigData.l7Profile "/mgmt/tm/ltm/profile/http/"
            }
            # Custom iRules: iRulesList[].name or iRulesList[] as strings
            if ($ConfigData.iRulesList) {
                foreach ($rule in $ConfigData.iRulesList) {
                    $ruleName = if ($rule.name) { $rule.name } elseif ($rule -is [string]) { $rule } else { $null }
                    if ($ruleName -and $ruleName -match "^/Common/" -and -not (Test-IsSsloGenerated -ObjPath $ruleName)) {
                        Add-Ref "irule" $ruleName "/mgmt/tm/ltm/rule/"
                    }
                }
            }
            # LTM policies: cpmPolicies[]
            if ($ConfigData.cpmPolicies) {
                foreach ($pol in $ConfigData.cpmPolicies) {
                    $polPath = if ($pol -is [string]) { $pol } elseif ($pol.name) { $pol.name } else { $null }
                    if ($polPath -and $polPath -match "^/Common/") {
                        Add-Ref "ltm_policy" $polPath "/mgmt/tm/ltm/policy/"
                    }
                }
            }
            # Access profile
            if ($ConfigData.accessProfile -and $ConfigData.accessProfile -match "^/Common/" -and -not (Test-IsSsloGenerated -ObjPath $ConfigData.accessProfile)) {
                Add-Ref "access_profile" $ConfigData.accessProfile "/mgmt/tm/apm/profile/access/"
            }
        }
        
        # SECURITY_POLICY: datagroups and URL categories referenced in rule conditions
        # Field paths from bigip_sslo_config_policy.py condition handling
        "SECURITY_POLICY" {
            $allRules = @()
            if ($ConfigData.rules) { $allRules += $ConfigData.rules }
            
            foreach ($rule in $allRules) {
                foreach ($condition in $rule.conditions) {
                    if (-not $condition.options) { continue }
                    $opts = $condition.options
                    
                    # Datagroups in subnet match conditions: options.subnet[].subnet where valueType=datagroup
                    if ($opts.subnet) {
                        foreach ($entry in $opts.subnet) {
                            if ($entry.valueType -eq "datagroup" -and $entry.subnet -and $entry.subnet -match "^/Common/") {
                                Add-Ref "datagroup" $entry.subnet "/mgmt/tm/ltm/data-group/internal/"
                            }
                        }
                    }
                    
                    # URL categories in category lookup conditions: options.category[]
                    if ($opts.category) {
                        foreach ($catName in $opts.category) {
                            if ($catName -and $catName -ne "") {
                                [void]$script:_depRefs.Add([ordered]@{ Type = "url_category"; Path = $catName; Endpoint = "/mgmt/tm/sys/url-db/url-category/" })
                            }
                        }
                    }
                }
            }
        }
    }
    
    return ,@($script:_depRefs)
}

# Capture external object references from snapshot blocks
# Returns: @{ objects = array of dependency records }
function Invoke-DependencyCapture {
    param([array]$OutputBlocks)
    
    Write-LogStep "Recording dependencies..."
    
    $depMap = @{}  # Keyed by path for deduplication
    
    foreach ($entry in $OutputBlocks) {
        $dataPropertyId = $script:TYPE_PROPERTY_MAP[$entry.deploymentType]
        if (-not $dataPropertyId) { continue }
        
        $configData = $null
        foreach ($prop in $entry.block.inputProperties) {
            if ($prop.id -eq $dataPropertyId) {
                $configData = $prop.value
                break
            }
        }
        if ($null -eq $configData) { continue }
        
        $refs = Get-BlockDependencyRefs -DeploymentType $entry.deploymentType -DeploymentName $entry.deploymentName -ConfigData $configData
        if (-not $refs -or $refs.Count -eq 0) { continue }
        
        foreach ($ref in $refs) {
            $path = $ref.Path
            
            # Deduplicate - key by type:path so cert and key for same name don't collide
            $dedupKey = "$($ref.Type):${path}"
            if ($depMap.ContainsKey($dedupKey)) {
                if ($entry.deploymentName -notin $depMap[$dedupKey].referencedBy) {
                    $depMap[$dedupKey].referencedBy += $entry.deploymentName
                }
                continue
            }
            
            # Fetch from source - full config capture for recreatable types
            $dep = $null
            if ($ref.Type -in @("certificate", "key", "ca_bundle")) {
                # Cert/key/CA bundle - name only, no content, no secrets
                # Non-portable: these must be pre-installed on the target
                $dep = [ordered]@{
                    type       = $ref.Type
                    path       = $path
                    endpoint   = ""
                    portable   = $false
                    config     = $null
                }
            } elseif ($ref.Type -eq "url_category") {
                # Only capture custom URL categories - built-in always exist on any licensed BIG-IP
                # Built-in list from Ansible bigip_sslo_config_policy.py condition_category_list
                if (-not $script:BUILTIN_URL_CATEGORIES.ContainsKey($path)) {
                    $catEncoded = $path -replace " ", "%20"
                    $catResult = Invoke-F5Get -Endpoint "/mgmt/tm/sys/url-db/url-category/~Common~${catEncoded}"
                    $catConfig = $null
                    if ($catResult.Success) {
                        $catConfig = Remove-DepRuntimeFields -Config $catResult.Response
                    }
                    $dep = [ordered]@{
                        type       = "url_category"
                        path       = $path
                        endpoint   = "/mgmt/tm/sys/url-db/url-category/~Common~${catEncoded}"
                        portable   = $true
                        config     = $catConfig
                    }
                }
            } elseif ($ref.Type -eq "monitor") {
                # Monitor requires type probing to determine the correct endpoint
                $dep = Get-MonitorDependency -MonitorPath $path
                if ($dep -and $dep["config"]) {
                    $dep["config"] = Remove-DepRuntimeFields -Config $dep["config"]
                }
            } elseif ($ref.Type -eq "datagroup") {
                # Datagroups - full capture including records for accurate recreation
                $dgName = $path -replace "^/Common/", ""
                $dgResult = Invoke-F5Get -Endpoint "/mgmt/tm/ltm/data-group/internal/~Common~${dgName}"
                if ($dgResult.Success) {
                    $dep = [ordered]@{
                        type       = "datagroup"
                        path       = $path
                        endpoint   = "/mgmt/tm/ltm/data-group/internal/~Common~${dgName}"
                        portable   = $true
                        config     = Remove-DepRuntimeFields -Config $dgResult.Response
                    }
                }
            } else {
                # All other types - fetch full config for snapshot completeness
                $objName = $path -replace "^/Common/", ""
                $endpoint = $ref.Endpoint
                $fullEndpoint = "${endpoint}~Common~${objName}"
                $result = Invoke-F5Get -Endpoint $fullEndpoint
                if ($result.Success) {
                    $dep = [ordered]@{
                        type       = $ref.Type
                        path       = $path
                        endpoint   = $fullEndpoint
                        portable   = ($ref.Type -in $script:PORTABLE_DEP_TYPES)
                        config     = Remove-DepRuntimeFields -Config $result.Response
                    }
                }
            }
            
            if ($dep) {
                $dep["referencedBy"] = @($entry.deploymentName)
                $depMap[$dedupKey] = $dep
            }
        }
    }
    
    # Sort dependencies by type (infrastructure -> objects -> policy -> data) then by path
    $depSortOrder = @{
        "certificate"     = 1
        "key"             = 2
        "ca_bundle"       = 3
        "vlan"            = 4
        "monitor_tcp"     = 5
        "monitor_http"    = 6
        "monitor_https"   = 7
        "monitor_icmp"    = 8
        "monitor_unknown" = 9
        "profile_tcp"     = 10
        "profile_http"    = 11
        "cipher_rule"     = 12
        "cipher_group"    = 13
        "log_publisher"   = 14
        "snatpool"        = 15
        "gateway_pool"    = 16
        "irule"           = 17
        "ltm_policy"      = 18
        "access_profile"  = 19
        "datagroup"       = 20
        "url_category"    = 21
    }
    $depList = @($depMap.Values | Sort-Object { 
        $order = $depSortOrder[$_.type]
        if ($null -eq $order) { $order = 99 }
        $order
    }, { $_.path })
    
    if ($depList.Count -gt 0) {
        Write-LogOk "$($depList.Count) dependencies recorded"
    } else {
        Write-LogOk "No external dependencies found"
    }
    
    return [ordered]@{
        capturedAt = (Get-Date -Format "yyyy-MM-dd HH:mm:ss")
        objects    = $depList
    }
}


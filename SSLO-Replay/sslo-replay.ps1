# Entry point: connection, session and menu loops
function Main {
    Clear-Host
    
    Initialize-SslBypass
    
    # Initial connection (with retry loop)
    while ($true) {
        if (Initialize-RemoteConnection) { break }
        Write-Host ""
        $retry = Read-Host "  Retry connection? (yes/no) [yes]"
        if ([string]::IsNullOrWhiteSpace($retry)) { $retry = "yes" }
        if ($retry -ne "yes") {
            Write-Host ""
            Write-Host "  Exiting." -ForegroundColor White
            Write-Host "  Latest version: https://github.com/hauptem/F5-SSL-Orchestrator-Tools" -ForegroundColor Cyan
            Write-Host "  API reference: https://github.com/F5Networks/f5-ansible-bigip" -ForegroundColor Cyan
            Write-Host ""
            return
        }
    }
    
    Start-Sleep -Seconds 2
    
    # Session loop
    while ($true) {
        # Menu loop
        while ($true) {
            Show-MainMenu
            
            $choice = Read-Host "  Select [0-4]"
            
            switch ($choice) {
                "1" { Invoke-SsloDump }
                "2" { Invoke-SsloRestore }
                "3" { Invoke-TopologyRedeploy }
                "4" { break }
                "0" {
                    Write-Host ""
                    Write-Host "  Exiting." -ForegroundColor White
                    Write-Host "  Latest version: https://github.com/hauptem/F5-SSL-Orchestrator-Tools" -ForegroundColor Cyan
                    Write-Host "  API reference: https://github.com/F5Networks/f5-ansible-bigip" -ForegroundColor Cyan
                    Write-Host ""
                    return
                }
                default {
                    Write-LogWarn "Invalid selection."
                    Press-EnterToContinue
                }
            }
            
            if ($choice -eq "4") { break }
        }
        
        # Clear host-specific state, keep credentials
        Clear-Host
        $script:RemoteHost = ""
        $script:RemoteHostname = ""
        $script:TMOSVersion = ""
        $script:SSLOVersion = ""
        
        while ($true) {
            if (Initialize-RemoteConnection -HostOnly) { break }
            Write-Host ""
            $retry = Read-Host "  Retry connection? (yes/no) [yes]"
            if ([string]::IsNullOrWhiteSpace($retry)) { $retry = "yes" }
            if ($retry -ne "yes") {
                Write-Host ""
                Write-Host "  Exiting." -ForegroundColor White
                Write-Host "  Latest version: https://github.com/hauptem/F5-SSL-Orchestrator-Tools" -ForegroundColor Cyan
                Write-Host "  API reference: https://github.com/F5Networks/f5-ansible-bigip" -ForegroundColor Cyan
                Write-Host ""
                return
            }
        }
        
        Start-Sleep -Seconds 2
    }
}

Main
